Leask man charged in mass murder
By Don Harvey
-
Saskatoon Star Phoenix
August 21, 1967
NORTH
BATTLEFORD (CP) - The RCMP announced Sunday that Victor Ernest
Hoffman, 21, of the Leask district has been arrested in the slaying
of nine members of the James Peterson family and will be arraigned
in district judge&#39;s court in North Battleford today.
Inspector Brian
Sawyer, in charge of the investigation, said Hoffman surrendered
without a struggle at his home about 5:30 p.m. CST Saturday, some
three hours after the funeral of Mr. and Mrs. Peterson and seven of
their nine children at Shell Lake.
The family had
been shot to death in their farm home near Shell Lake, 65 miles
northeast of here, early Tuesday.
Inspector
Sawyer said police found a .22-claibre Belgian Browning pump-action
repeater rifle Friday and that tests at the RCMP ballistics
laboratory in Regina &#34;positively identified it as the murder
weapon.&#34;
The inspector
said Hoffman will be formally charged at today&#39;s hearing with the
murder of Mr. Peterson, 47. He said the accused would probably be
remanded to the Saskatchewan Hospital in North Battleford for mental
examination.
Other victims
of the slaying were Mr. Peterson&#39;s wife Evelyn, 42, and their
children, Jean, 17; Mary, 13; Dorothy, 11; Pearl, 9; William, 5;
Colin, 2; and Larry, 1.
Four-year-old
Phyllis, sleeping under the bedclothes between her two sisters, was
unharmed.
The inspector
said no motive had been established for the slaying.
&#34;The arrest was
the culmination of a very casual conversation that one of our
members had with an individual and we decided it would be wise to
check the Hoffman residence.&#34;
Leask is about
60 miles southeast of Shell Lake, a mixed farming community of about
250 persons.
Sawyer said the
RCMP has advised Hoffman&#39;s parents to retain a lawyer for their son.
The inspector
described Hoffman as single and of average build, about five feet
eight inches tall and weighing about 165 pounds.
He said Hoffman
&#34;was not a bit scared&#34; and behaved normally since his arrest.
Inspector
Sawyer paid tribute to all the RCMP men who had worked on the case.
&#34;This makes me
very, very proud of being a member of this force, &#34;he said. &#34;Credit
goes to the 75 men we had in the area as well as to many more at
other detachments elsewhere.&#34;
He said most of
the men in the immediate area had put in at least 18 hours a day
since Tuesday. A few had worked as much as 36 hours without a break.
The mass
slaying was discovered about 9 a.m. Tuesday by a neighbor who called
at the isolated four-room house to ask Mr. Peterson for help with
his haying.
Police said Mr.
Peterson&#39;s body, clad only in undershorts, lay facing the kitchen
door. The bodies of Mrs. Peterson and the baby were found in the
yard and the remaining children were in bed. Each had been shot at
least twice with .22-claibre bullets, many of them at close range.
Police said 28
bullets were fired, of which 27 found their mark.


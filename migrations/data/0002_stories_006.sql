On August 19, 1967, Hoffman was arrested by the Royal Canadian Mounted Police without putting up resistance. He was found at his parents&#39; home in Leask, about 65 km (40 mi) southwest of Shell Lake.[2] After his arrest he told the police that he had fought the devil before the murders and described him as being &#34;tall, black and having no genitals.&#34;[6] He was remanded to a mental hospital in North Battleford where he was diagnosed with paranoid schizophrenia.
Hoffman was found not guilty by reason of insanity on non-capital murder charges in February 1968. During the trial, Crown prosecutor Serge Kujawa called Hoffman &#34;the craziest man in Saskatchewan.&#34;[1] He was put under the custody of the provincial Health Ministry and sent to a mental institution. He remained most of the time in an Ontario-based institution until December 2001, when he was granted supervised access to the towns of Penetanguishene, Port McNicoll and Midland in Ontario.[6] This decision was not without controversy since the hospital was only required to inform the local police of Hoffman&#39;s release.
Canadian journalist Peter Tadman wrote a book about the murders in 1992 and had the chance to interview Hoffman several times. According to Tadman, Hoffman felt no guilt about the murders and reported that he still saw the devil that compelled him to commit them.
Hoffman died of cancer under custody on May 21, 2004.
In popular culture[edit]
Manitoba country musician Irvin Freese took an interest in the event, writing and recording &#34;The Shell Lake Disaster&#34; which was released as a 45 on Winnipeg&#39;s Eagle Records label (ER-128) within a month of the event (September 1967). Lawyers[clarification needed] threatened the label with a lawsuit, and the 45 was rapidly recalled. This rarity has recently been released on &#34;The Best Of Eagle Records&#34; on the Super Oldies label (2009).[7][non-primary source needed]

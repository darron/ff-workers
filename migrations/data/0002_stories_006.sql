Nine in farm family slain
Saskatoon Star Phoenix
August 16, 1967
SHELL LAKE - Fourteen shots from a .22-calibre
weapon snuffed out the lives of nine of 10 members of a family here
sometime Monday night.
The mystery killer gunned down James Peterson, 47
years, his wife Evelyn, 42, and seven of the eight children who were
at his home. The children were aged one to 17 years.
Bodies were found on the front porch, the back
yard and in the home. Nearly all of the children were killed in
their beds.
The dead children are: Larry, 1 1/2 years; Colin,
3; William, 6; Pearl, 9; Dorothy, 11; Mary, 13; Jean, 17.
The lone survivor of the massacre was four-year-old
Phyllis.
Phyllis, according to one report, was lying in a
bed between her brothers and sisters and may have slept through the
entire blood bath.
The RCMP officers in numbers described by one
officer as &#34;One hell of a lot&#34; are combing the area for the killer
or killers.
RCMP set up roadblocks and brought in the dogs to
search nearby bush. Inspector Brian Sawyer said a search party of up
to 250 persons was &#34;ready to go.&#34;
The only confirmed clue turned up so far are five
spent cartridge casings. An unconfirmed report told of a .22-calibre
weapon being found by youngsters but this has so far been denied by
police.
A neighbor, W. J. Lange, discovered the lonely
farm house killings when he called on Mr. Peterson to begin haying
operations at about 9 a.m. Tuesday.
Mr. Lange opened the door to the Peterson home to
kid Mr. Peterson that he had slept in when he saw Mr. Peterson&#39;s
body by the kitchen door. The house was not ransacked.
Mr. Lange found the keys in the Peterson family&#39;s
1957 station wagon and drove the car to the nearest telephone at
Shell Lake, four miles away.
Coronor Dr. Calvin S. Lambert estimated the time
of death at &#34;possibly 2 a.m.&#34; A coronor&#39;s jury, called to the scene
to view the bodies, estimated 14 shots had been fired.
Ed Simonar, who operates a garage at Shell Lake,
said powder burn indicated the victims were shot, at close range.
RCMP said there was no apparent motive for the
slayings, and the coronor said that, because no weapon was found, &#34;I
don&#39;t think there was a suicide.&#34;
The bodies of Mrs. Peterson and 1 1/2-year-old
Larry were found in the back yard under an open window.
At the scene Tuesday night, RCMP officers were on
their hands and knees going through long grass of the farm yard
looking for anything.
A non-commissioned officer on the scene said
point blank, as he stood over a child&#39;s headless doll, &#34;This is the
worst I have ever seen.&#34;
More officers were inside the tiny, white,
five-room homestead. Chickens pecked in the front yard and pink pigs
roosted in a pen behind the house.
The family dog, a black mongrel with white
markings cowered under the back porch while police searched for
clues as to who killed its masters. The dog never wagged his tail.
A neighbor had taken the rest of the
stock---otherwise the tiny home looked like it always has on a hot
summer afternoon. A boy&#39;s bicycle was in the dirt yard, a wash rag
was hanging on the clothes line, and an old washing machine held
blooming pink flowers.
Inside the home was death.
Through the garden, under the clothesline and
through the pines another officer with a tracking dog searched.
One officer hinted at a major clue that had not
been mentioned previously but would not be pressed on the point.
Overheard from another tight-lipped constable was
that one of the children had been shot three times in the head and &#34;didn&#39;t
move a muscle,&#34; as she lay in bed.
The house so far is out of bounds to all but
police officers.
The neighbor who found the bodies had to go four
miles to the nearest phone to inform the RCMP in Spiritwood, 16
miles away. Since that time officers from all over the province have
been called in on the manhunt.
The little survivor is being kept at the home of
an uncle, Helmer Helgeton, 3 1/2 miles away from the death house.
Another daughter, Mrs. Kathy Hill, 20, is en route from British
Columbia.
RCMP Inspector Brian Sawyer of North Battleford,
in charge of the investigation, described the area as slightly
rolling with newly developed prosperous although small grain farms.
The area is interspersed with long stretches of heavy scrub bushland.
The wooded areas were comlicating the
investigation by more than a dozen RCMP personnel. More were being
readied to move into the area early Wednesday to expand the area of
inquiry.
&#34;There are countless hundreds of inquiries to be
made,&#34; Inspector Sawyer said in an interview from North Battleford,
80 miles southwest of Shell Lake. The tiny community is 50 miles
west of Prince Albert, the nearest city.







Attractions[edit]
Buffalo Narrows is notable for its scenery and its bridge that crosses the Kisis Channel.[18] The bridge was officially opened in 1981 replacing a ferry that was set up in 1957. This channel links Peter Pond Lake and Churchill Lake.
Buffalo Narrows Sand Dunes Park[edit]
The dunes of Buffalo Narrows Sand Dunes Park located 5 kilometres (3.1 mi) north of downtown create a 10 kilometres (6.2 mi) fine sandy beach along the southwest shore of Big Peter Pond Lake (55°53′35.90″N 108°36′39.64″W﻿ / ﻿55.8933056°N 108.6110111°W﻿ / 55.8933056; -108.6110111﻿ (Buffalo Narrows Sand Dunes Park)). The park, which is 3,650 hectares (14.1 sq mi) in size, includes the peninsula that nearly cuts Peter Pond Lake in two creating Little Peter Pond, the southern section and Big Peter Pond, the northern section. Formerly Crown land, the recreation area was transferred to the northern village of Buffalo Narrows in 2003. The community has assumed the responsibility for its maintenance and development.[19][20]
The beach is a nesting site of the vulnerable sparrow-sized piping plover.[21]

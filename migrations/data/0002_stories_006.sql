The Shell Lake murders is the name of a mass murder incident committed by Victor Ernest Hoffman (1946 – May 21, 2004) in Shell Lake, Saskatchewan, Canada, during the early morning of August 15, 1967. Nine people, all members of the family of James Peterson, were shot in the head by a man who was later called &#34;Canada&#39;s worst random mass murderer.&#34;[1]
Events[edit]
Victor Hoffman was 21 years old at the time of the murders and had been released from a mental hospital just three weeks prior. On the morning of August 15, he entered the Petersons&#39; farm armed with a .22-calibre Browning pump-action repeater rifle.[2] He then proceeded to shoot all but one of the present members of the Peterson family, seven of them children, at close range around the four-room house. According to police, 28 shots were fired in total, of which 27 found their target.
James Peterson was shot in the kitchen, while his wife Evelyn and her one-year-old baby were found in the backyard. The other six children were shot while sleeping in their bedrooms. Their ages ranged from 2 to 17 years old. Phyllis Peterson, then 4 years old, was the lone survivor of the massacre.[3] She was sleeping under the bedclothes between her two sisters and was not noticed by Hoffman. However, Hoffman later declared that he spared her because &#34;she had the face of an angel.&#34;[4]
The bodies were found by Wildrew Lang who was to help James Peterson with farm duties later that morning. He had to travel 6 km (3.7 mi) to the next telephone post before he could report the incident to the police. The police immediately started an extensive manhunt on the surroundings of the house.
Victims[edit]
James Peterson, age 47
Evelyn Peterson, 42
Jean Peterson, 17
Mary Peterson, 13
Dorothy Peterson, 11
Pearl Peterson, 9
William Peterson, 5
Colin Peterson, 2
Larry Peterson, 1
The Peterson&#39;s oldest daughter, Kathy Peterson Hill (then age 19 or 20) was married and living in British Columbia at the time of the murders.[3] She moved back to the Peterson farm and took custody of her youngest sister.[5]
Aftermath[edit]

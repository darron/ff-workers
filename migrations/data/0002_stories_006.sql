Canadian Pacific Air Lines Flight 21 was a scheduled domestic flight from Vancouver, British Columbia, Canada, to Whitehorse, Yukon, Canada, via Prince George, Fort St. John, Fort Nelson and Watson Lake on July 8, 1965. The Douglas DC-6B plane crashed near 100 Mile House, British Columbia, taking the lives of all 52 aboard.[1][2] An inquest determined that the explosion was the result of a bomb, but the crime remains unsolved.[1]
Incident[edit]
While en route from Vancouver to Prince George the DC-6 Empress of City of Buenos Aires, piloted by World War II veteran John &#39;Jack&#39; Steele,[3] crashed after passing Ashcroft, British Columbia. About 15:40, three mayday calls were heard by air traffic control in Vancouver. An explosion had occurred in the left aft lavatory. The tail separated from the fuselage. The aircraft spiralled and crashed into a wooded area.  All 46 passengers and 6 crew perished.[2]  The crash site is 40 km (25 mi) west of 100 Mile House.[1] Remnants of the DC-6 remain at the crash site near Dog Creek.[4]
Aftermath[edit]
A coroner&#39;s inquest concluded &#34;an explosive substance foreign to the normal contents of the aircraft&#34; caused the crash.  A witness on the ground saw the tail of the aircraft separate from the fuselage and debris trail out behind the aircraft. The debris turned out to be the bodies of passengers forced out by the depressurization of the aircraft.  The fuselage was consumed by fire where it fell, but the tail, found 500 metres away, was not.  Rescue crews reached the crash site while the fire continued to burn but no survivors were found.  Crash investigators found traces of acid that led them to believe a bomb in the lavatory was involved.  Traces of potassium nitrate and carbon, consistent with a &#34;low-velocity explosion&#34; were found. Gunpowder or stumping powder causes a low-velocity explosion.  The explosion damaged bulkheads in the lavatory, severed pipes in the tail and tore a metre-wide hole in the side of the fuselage.  The Royal Canadian Mounted Police investigation focused on four passengers although none was a suspect. No one claimed responsibility and no charges were ever laid. The source of the explosion remains unknown.[1]
2018 investigation[edit]
The crash was re-examined by experts during six episodes of the second season of the CBC true crime podcast Uncover.[5]

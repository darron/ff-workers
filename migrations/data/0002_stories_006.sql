The Shell Lake
murders is the name of a single mass murder incident committed
by Victor Ernest Hoffman (b. 1946, d. May 21, 2004) in Shell Lake,
Saskatchewan, during the early morning of August 15, 1967.
Nine
people, all members of James Peterson&#39;s family, were shot in the
head by a man who was later called &#34;Canada&#39;s worst random mass
murderer.&#34;
Victor Hoffman was
21 years old at the time and had been released from a mental
hospital just three weeks before the murders. In the morning of
August 15 he entered the Peterson&#39;s farm armed with a .22-calibre
Browning pump-action repeater rifle.
He then proceeded to shoot all
members of the Peterson family, seven of them children, at close
range around the four-room house. According to police 28 shots were
fired in total, of which 27 found their target.
Mr. Peterson was
shot in the kitchen, while his wife Evelyn and her one-year old baby
were found in the backyard. The other six children were shot while
sleeping in their bedrooms. Their ages ranged from 2 to 17 years
old.
Phyllis Peterson, then 4 years old, was the lone survivor of
the massacre. She was sleeping under the bedclothes between her two
sisters and thus was not noticed by Hoffman. However, Hoffman later
declared that he spared her because &#34;she had the face of an angel.&#34;
The bodies were
found by a neighbor who was to help Mr. Peterson with farm duties
later that morning. He had to travel 6 km (3.7 mi) to the next
telephone post before he could report the incident to the police.
The police immediately started an extensive manhunt on the
surroundings of the house.
Aftermath
On August 19,
1967, Hoffman was arrested by the RCMP without putting up
resistance. He was found at his parents&#39; home in Leask, about 65 km
(40 mi) southwest of Shell Lake. After his arrest he told the police
that he had fought the devil before the murders and described him as
being &#34;tall, black and having no genitals.&#34; He was remanded to a
mental hospital in North Battleford where he was diagnosed with
paranoid schizophrenia.
Hoffman was found
not guilty by reason of insanity on non-capital murder charges in
February 1968. During the trial Crown prosecutor Serge Kujawa called
Hoffman &#34;the craziest man in Saskatchewan.&#34; He was put under the
custody of the provincial Health Ministry and sent to a mental
institution.
He remained most of the time in an Ontario-based
institution until December 2001, when he was granted supervised
access to the towns of Penetanguishene, Port McNicol and Midland in
Ontario. This decision was not without controversy since the
hospital was only required to inform the local police of Hoffman&#39;s
release.
Canadian
journalist Peter Tadman wrote a book about the murders in 1992 and
had the chance to interview Hoffman several times. According to
Tadman, Hoffman felt no guilt about the murders and reported that he
still saw the devil that compelled him to commit them.
Hoffman died of
cancer under custody on May 21, 2004.

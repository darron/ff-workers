Governance[edit]
As set out by the Northern Saskatchewan Municipal Government Act, and overseen by Municipal Relations, the village is governed by six Councillors, who are elected at-large every four years. Robert Woods is the mayor.[2]
Transportation[edit]
Buffalo Narrows is connected to the rest of Saskatchewan by Highway 155. Highway 155 is paved along its whole length. The community is served by the Buffalo Narrows Airport and the Buffalo Narrows Water Aerodrome.
Financial Services[edit]
Innovation Credit Union has an Advice Centre located at #3-1491 Pederson Ave. The location includes a full service automated teller machine.[22]
Education[edit]
The school in Buffalo Narrows is called Twin Lakes Community School or &#34;TLCS&#34;. The K-12 school has approximately 310 students and about 30 teachers and is part of the Northern Lights School Division # 113.[21] Northlands College maintains a satellite campus in Buffalo Narrows.

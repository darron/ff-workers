Buffalo Narrows (Plains Cree: ᒧᐢᑐᓱ ᐘᐹᓯᕽ, romanized: mostoso-wapâsihk) is a northern village in Saskatchewan, Canada. It is a community of 1,110 people.[3] Located at the &#34;Narrows&#34; between Peter Pond Lake and Churchill Lake, tourism and resource extraction (logging, fishing) are its main economic activities.
History[edit]
John Franklin&#39;s Coppermine Expedition map of 1819–1822 showing the fur trade route from Île-à-la-Crosse to Methye Portage Buffalo Narrows or Détroit du Boeuf as it was called in French was founded in the early 20th century as trapping, mink ranching and fishing settlement by Scandinavian traders.[7]
The community developed around an earlier Dene settlement. John Macoun, who was travelling the fur trade route from Methye Portage to Lac Île-à-la-Crosse visited the &#34;Narrows&#34; in September 1875. He described his visit to this old community in the following lines: &#34;After sunrise, a headwind sprang up with which we battled all day and reached the &#34;Narrows,&#34; that connect Buffalo and Clearwater lakes, before dark. &#34;Chipewyan House&#34; is situated at the eastern end of the &#34;Narrows,&#34; and at the head of Clearwater Lake. Here the Chipewyans have built themselves several houses.&#34;[8]
Chipewyan House where Macoun stayed for about four days was probably a small outpost of the Hudson&#39;s Bay Company much like Buffalo House at Dillon and Bull&#39;s House on the La Loche River.
Clearwater Lake (or Clear Lake) was renamed Churchill Lake in 1944 and Buffalo Lake was renamed Peter Pond Lake in 1932.
1969 axe massacre[edit]
Main article: Buffalo Narrows murders
On 30 January 1969, the community was the site of the killing of the Pedersen family, and their friend John Herman. Seven people were killed by 19-year-old Métis labourer Frederick Moses McCallum (b. 1949), who was arrested, diagnosed with schizophrenia and found not guilty by reason of insanity. As of February 2019, he lives in Ontario.[9]
Demographics[edit]
In the 2021 Census of Population conducted by Statistics Canada, Buffalo Narrows had a population of 1,014 living in 373 of its 449 total private dwellings, a change of -8.6% from its 2016 population of 1,110. With a land area of 68.04 km2 (26.27 sq mi), it had a population density of 14.9/km2 (38.6/sq mi) in 2021.[10]

A series of deadly shootings in Langley, B.C., started about six hours before RCMP issued a cellphone alert warning the public.
RCMP Sgt. David Lee says the alert, which was issued around 6:20 a.m., was done &#34;at the appropriate time&#34; when police were able to tie the events together.
Two men were found dead, a woman was critically hurt and another man was shot in the leg at separate scenes around the Metro Vancouver community.

McKenzie murders
In the 1840s, large numbers of peasant farmers fled Famine Ireland for British North America. Many joined the sizeable Protestant and Catholic Irish communities in the area of Saint John, New Brunswick. One member of that community was Patrick Slavin, a man in his 50s or 60s who had worked constructing the European and North American railway and who in 1857 worked as a labourer.
His associate was a younger man, Hugh Breen, born in Oromocto, another labourer. Slavin lived in a cabin several miles from Saint John, in Simonds parish, with his wife and children, one of whom was teenaged Patrick Jr. In the weeks prior to the crime, Breen had boarded with the Slavins.
Persons of Irish birth and ethnicity were an important presence in Saint John and the surrounding county. Although they drew more attention, Potato Famine migrants of the 1840s, according to the 1851 census, were outnumbered by earlier immigrants. In 1871 more than half of the population of the city of 23,315 were ethnically Irish. The neighbouring communities of Portland and Simonds also included many immigrant and second-generation Irish, many of them Protestant. Simonds parish was a largely rural community that lay between Saint John and the coastal community of St. Martin&#39;s.
Another resident of the area was Robert McKenzie, a Scottish immigrant and businessman who was reputed to have a sizeable personal fortune. In 1857 he lived with his young family at his farm near Beaver Lake, in the Mispec area, and operated a chair factory. On a Sunday morning in October, a local man discovered nothing left of McKenzie&#39;s farm on the Black River Road but smouldering ruins; a search of the burned buildings revealed the bodies of husband, wife and one child.
The investigation
A local magistrate directed search of the ruins. Two adults and four children were unaccounted for, but only the partial remains of the two parents and one child were found. The discovery of the bodies and of an open but empty safe, and the fact that both the main house and the farm labourer’s house had burned, created suspicion in the minds of local magistrates and the Saint John police. McKenzie, who in addition to owning a business and a large farm was a money lender, was rumoured to be fairly wealthy.
All these circumstances led to the conclusion that there had been violence done, and suspicion immediately fell on a man who had been in the neighbourhood and about McKenzie&#39;s some days previous but who had subsequently disappeared. This man who gave his name while at McKenzie&#39;s as WILLIAMS, but who was elsewhere known as BREEN or GREEN or McGUIRE had engaged with McKenzie to come and hire with him, his former hired man having just left to go to Canada and had left McKenzie&#39;s on the day previous, Friday; saying that he would be back Saturday night with his wife and furniture. He appears, however, to have gone instead to the house of a man named SLAVIN, three or four miles distant on another road, where he had been residing of and on for the previous four or five weeks and where, there is no doubt, from what has since transpired, that the crime was planned.
At the Coroner&#39;s inquest, the most important evidence came from the John Slavin, the young son of Patrick, who testified that on the night of the murders, robbery and arson, his father, older brother Pat and Hugh Breen had disappeared then returned with clothing, money and other property, and acted suspiciously. Testimony from other witnesses corroborated this information.
Mrs. Slavin had attempted to shift the suspicion of the inquest jury to Breen. The Saint John police eventually tracked down the three suspects hiding in the woods ten miles from Slavin&#39;s home in Simonds parish. They had been aided and abetted by another Irish family, which was related to the Slavins through marriage. The police eventually found close to eighty sovereigns in cash. The Coroner&#39;s jury returned a verdict of wilful murder against the suspects.
Trial of Patrick Slavin Jr.
The McKenzie murders were unprecedented for their brutality; an entire family, including an infant only one year old, had been killed by an axe. The Grand Jury found true bills against the three prisoners for the murder of McKenzie and his wife and children. Breen and the elder Slavin entered guilty pleas; only young Patrick, who appeared to have been an accessory to murder, was placed on trial.
Pat&#39;s major role appeared to have been keeping watch, but testimony indicated that he may have entered the farmhouse after the killings to search for loot. He was assigned, by the trial judge, an able barrister, Davis S. Kerr, who objected to reports that he had been actually hired by the defendants. He was assisted by A.R. Wetmore. The elder Slavin was formally charged with the murder of Mrs. Effie McKenzie and of an unnamed child of Robert and Effie McKenzie. He pleaded guilty to both counts.
Although the law held teenagers to be legally responsible, in practice juries were loathe to subject them to the full wrath of the criminal sanction. The prosecution was conducted by Solicitor General Charles Fisher, who explained that Pat&#39;s statement was the most important evidence, assisted by Attorney General Charles Watters. Under English criminal law, young Slavin, although he had not taken part in the violence, was as guilty as his father. A neighbour who had employed Pat Slavin testified that he was &#39;not a bad boy&#39; although he lacked common sense.
Several witnesses described him as &#39;simple.&#39; The Slavin family, who lived in a log hut in upper Loch Lomond, was described as poor. Pat, who had been born in Ireland, had arrived in New Brunswick in the late 1840s. Young John Slavin repeated his damning evidence against his father and Breen, which suggested how they had planned the crime in advance. According to Saint John&#39;s chief of police, son Pat had blamed Breen for both planning the crime and carrying out the murders.
Hugh Breen&#39;s testimony
Despite Pat&#39;s attempt to place the blame on him, Breen, who had already pleaded guilty, did not implicate him in the violence. According to Breen, the youth helped to loot the house, helped to burn the two dwellings and carried loot home:
I went to the house and asked McKenzie to come down as my wife had come. Old Slavin said not to make a fire, and after it was made we put it out. McKenzie came down with a candle in his hand and old Slavin took the axe used for chopping wood out of my hand. His son was there. There were two or three in the house. Slavin was in one place and McKenzie in another.
When McKenzie came in, Slavin came out and said &#34;she is on hand&#34; (this was referring to my wife&#39;s coming). He had the axe in his hand and hit McKenzie on the breast, he said &#34;dead dogs tell no lies.&#34; He hit him on the breast with the back of the axe, McKenzie groaned but did not speak. We put him into the cellar and old Slavin afterwards brought him up.  When we went to the upper house Slavin asked me if I knew the house; he told me to go in and watch the door till he got a view of her. He then went in and Mrs. McKenzie was sitting at the fire with a child in her arms. She asked if she was coming (meaning my wife) Slavin then struck her with the axe on her head and she fell over the edge of the stone; he then struck her three blows on the body. He then struck the children who were standing round their mother crying, he killed the whole of them; the children cried, but Mrs. McKenzie did not. Prisoner and me took no direct part in the murder; we were standing in the porch at the time.
Testimony of Patrick Slavin Sr.
We went up to the house, Breen and I, I was first, Breen showed me the way. I did not take the axe. There was one at the door, Breen gave it to me. When I went in I saw Mrs. McKenzie sitting by the fire and the four children beside her. I struck her on the head 10 or 15 times. I killed them all. They did not cry much. We searched the house and found over 100 pounds of money. It was myself that ransacked the chest, the money was in gold. It was in a yellow bag, like the one in Court. There was a purse also and portmanteau. The boy was knocking about keeping watch. We had something to eat. We though it best to set fire to the houses. Breen and I both did it.
Defence counsel used the word &#34;extraordinary&#34; to described the situation where one self-confessed murderer was testifying for the Crown and another for the defence.
The defence planned to use testimony by &#34;old&#34; Slavin, despite his plea of guilty, to shift the blame on Breen, and to protect the boy. Pat was described as a simple boy, much in fear of his father, and unable to distinguish between right and wrong. Kerr and Wetmore argued that Pat had no prior knowledge of the murder plot, but was possibly guilty of conspiring to commit robbery.
On the question of his son&#39;s involvement, Slavin testified:
I have three boys Pat Slavin, 15 to 16, Johnnie Slavin, 10 to 11, and Jamie Slavin, 6 to 7, Pat knows right from wrong; I have sometimes been too severe to him. He did not know when we left what we were going to do; if he had refused to go, I would have made him. He is a tender hearted boy and has not looked the same since. It was I that killed them all. I am aware that the sentence of death will be passed on me. I have told the truth...
Verdict and sentencing
The Crown&#39;s summation stressed factual evidence, the statements of the accused and circumstantial evidence. Patrick Slavin Jr. was described as a boy of &#34;a most diabolical nature, and was not fit to let loose on this country.&#34; The prosecution disputed suggestions, supposedly fuelled by sentimentality, that the youth was insane or mentally incapable. The Attorney General commented that prior to this case he had opposed capital punishment. Judge Parker, in his charge to the jury, was overcome by emotion.
He dismissed the coercion defense offered by Kerr, and stated that the jury would have to decide whether or not the youth had the ability to distinguish right from wrong.
Judge Parker passed sentence on the two adults, who were scheduled to be hanged on December 11, 1857. Slavin stated that he was satisfied. The prisoners were reported as exhibited a &#34;firm demeanour&#34; throughout the entire proceedings.
The jury returned a verdict of guilty against Patrick Jr., with a recommendation for mercy. Sentencing was deferred. Judge Robert Parker&#39;s sentence contained a detailed justification, as if to underscore the sensitive nature of the case. He announced that he would recommend to the executive level of government a sentence of imprisonment to the provincial penitentiary. But in the meantime, he was sentenced to be hanged on March 4.
Deaths of Breen and Slavin Sr.
As Saint John prepared for a public double hanging, the case took an unexpected twist:
BREEN one of the McKENZIE murderers committed Suicide Sunday eve. last by hanging himself. He was found suspended by an old yellow silk handkerchief from a small piece of wood, one end of which rested on a shelf and the other on a cleet. A bit of wood, which had a hole bored in it and fitting on the stump of an old nail, assisted in the support of the cross stick. The place was too low for the purpose, unless he pressed the weight of his body on his throat by raising his feet. The only persons allowed to visit him alone were Mrs. DOHERTY, Mrs. CARVELL, Mrs. TRAVERS and Miss HAGERTY, the Sisters of Charity and the Priest. Mrs. Doherty was with him from half past six. About 20 minutes after she left, Miss CREIGHTON went to the wicket and called Breen but got no answer. The Priest stated in his evidence before the Coroner that he was visiting him a few minutes before 7 o&#39;clock. He had been called out in a hurry and on going to Breen&#39;s cell door, he saw him as if kneeling. He went towards him and finding he had hung himself, assisted some persons to take him down. We understand he was buried last night in the Almshouse Burying Ground.
The Carleton Sentinel was one of many newspapers to report Slavin&#39;s execution, which was stage outside the county jail, in full view of a large crowd. A detachment of soldiers from the British garrison, armed with rifles and bayonets, was on hand to keep order.
Yesterday being the day appointed for the execution, and the prisoner Patrick SLAVIN Senior still to dispose of, crowds began at an early hour to assemble in front of the Jail ... About ten o&#39;clock, the hour previously given notice of for the execution to take place, the crowd in front of the Jail could not have been less than 5 or 6 thousand. About ten minutes past ten, the prisoner stepped on the platform accompanied by Charles JOHNSTON, Esq., High Sheriff and Rev. SWEENY, Catholic Priest. There were also in attendance his Worship the Mayor, George BLATCH, Esq., Acting Deputy Sheriff, the Jailor THOMAS and a number of Magistrates of the City and County. The Sheriff himself performed the duty of adjusting the fatal noose and Constable PIDGEON having drawn the cap over the prisoner&#39;s face, he stepped forward on the drop and seemed for a minute either listening to, or accompanying Rev. Sweeny in prayer, during which time the Sheriff, having stepped inside, cut the support, the drop fell and death seemed to be almost instantaneous.
Postscript
As many predicted, the sentence of young Patrick Slavin was commuted to life in prison. Years later he managed to escape from the penitentiary on Courtenay Bay and fled across the border to Maine.
Slavin&#39;s mother, who had taken charge of some of the murderers&#39; ill-gotten gains, and who had assisted in hiding the fugitives, drew the attention of the press several weeks following the execution of her husband when she remarried. The groom was a man who had been charged with rape but acquitted in 1857.
Saint John County Courthouse, 2006
The old jail, which was demolished in the 1970&#39;s, was in the rear of the building. Patrick Slavin was hanged from a gallows platform built outside the jail.

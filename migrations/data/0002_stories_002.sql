Alexander &#39;Sandy&#39; Keith, Jr.
(1827�1875) was a notorious nineteenth century criminal from Halifax,
Nova Scotia.
Keith was born in 1827 in Caithness, Scotland,
immigrating to Halifax when he was a small boy. The nephew of Alexander
Keith, founder of the Alexander Keith&#39;s brewery, Keith worked for a time
as a clerk in his uncle&#39;s brewery.
Keith became a secret agent for the Confederate
States of America during the American Civil War, acting mostly as a
blockade runner and courier. He was involved with Luke Blackburn in an
infamous plot to send clothes infected with yellow fever to northern
cities in the United States.
In 1865, he swindled his associates-in-crime and fled
to St. Louis, Missouri, settling finally on the prairie. There, he
married Cecelia Paris, a milliner&#39;s daughter from St. Louis.
Hunted down by one of his victims, he fled again with
Cecelia to Germany, where they lived the high life in Dresden and
Leipzig, hobnobbing with wealthy socialites and Saxon generals under the
assumed name of William King Thomas. When the couple began to run
out of money, Keith concocted a plot to blow up passenger ships and
collect the insurance money.
This led to a major catastrophe in Bremerhaven, in
December 1875 when one of his bombs accidentally went off on a dock,
killing eighty people. At the time, the deed was known as the &#34;crime of
the century. Keith was aboard another ship at the time and was aware of
the premature detenation of his time bomb, and the massive carnage. He
went to his suite in the ship he was on and shot himself. He died in a
week&#39;s time.&#34;
After the tragedy was revealled as a murder/insurance
scam on a large scale, the disappearance of other ships were looked into
to see if Keith and his possible associates were involved. One was the
disappearance of the SS City of Boston which vanished in January 1870.

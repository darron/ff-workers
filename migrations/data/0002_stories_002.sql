The Dynamite Fiend
By Ann Larabee - St Louis Magazine
In the final months of the Civil War, a gentleman
took rooms in St. Louis� Southern Hotel. He was a bear of a man with a
huge appetite and memorably bad table manners. Flashing wads of cash, he
impressed the staff with tales of his speculations on Wall Street. He
intimated that he and the hotel steward, known as the Frenchman, had
been daring blockade runners together in the Bahamas. Most St. Louisans
who met Alexander King Thompson were completely taken in by his
amiability, generosity and dash.
But Thompson was not what he seemed. Ten years later,
his friends in the Southern Hotel would learn that they had laughed and
joked with the most devious mass murderer of the 19th century. The
amiable Alexander King Thompson hid an evil Mr. Hyde: Alexander �Sandy�
Keith Jr., a Confederate agent from Halifax, Nova Scotia, whose greed
would lead him to kill 81 people in a single blow and earn him the
nickname the �Dynamite Fiend.�
Sandy Keith began his ruthless career early. Born in
the Scottish Highlands in 1827, he immigrated to Canada when he was a
boy. He went to work in the brewery of his uncle, Alexander Keith, a
respected businessman and three-time mayor of Halifax. Envious of his
uncle�s wealth and power, the younger Keith began forging his uncle�s
name on phony bills of exchange and was probably responsible for blowing
up the Halifax powder magazine in 1857 to cover up a gunpowder swindle.
At the start of the Civil War, Southern blockade
runners began arriving in Halifax, eager for
adventure and profit. Keith saw his chance and became their
purchasing agent. He was so popular among the blockade runners that he
became known as the �Confederate Consul,� offering his wealthy new
friends the finest champagne in his rooms at the Halifax Hotel.
But even more devious operations were afoot in Halifax. As the war
became ever more desperate for the Confederacy, Southerners hiding in
Canada plotted raids and terrorist attacks across the border. Keith was
in the thick of it, and, because he had earned their trust, the
conspirators enlisted him in their schemes.
The most ruthless of these, at least in spirit, was organized by Dr.
Luke Blackburn, who would later become governor of Kentucky. The good
doctor volunteered his services to yellow-fever patients in Bermuda,
collected their soiled clothes and linens and had the goods sent through
Halifax to Northern cities for auction. Keith was involved in the
shipment of this 19th century version of bioterrorism. Luckily, it
failed; Blackburn was of the mistaken belief that yellow fever is
transmitted through human contact, but mosquitoes are the real carriers.
By the end of 1864, Confederate operatives and blockade runners were
eager to squeeze as much profit as they could from the war before their
inevitable defeat, and they heaped money in Keith�s hands. He feigned
enthusiasm for their cause while secretly plotting to destroy them.
Inspired by Confederate bomb makers such as Thomas Courtenay, a St.
Louis insurance salesman who invented a device with which to blow up
Union steamships, Keith began to form an idea: He would use time bombs
to blow up ships and collect the insurance money.
Some later believed that Keith had used a bomb to scuttle a ship
carrying the theatrical costumes of John Wilkes Booth, who performed in
Montreal in 1864.
To the surprise of his Confederate chums, Keith vanished in December
1864, taking with him $1 million worth of investments and Mary Clifton,
a chambermaid from the Halifax Hotel. For a brief time, he hid in Boston
and New York, enjoying a lavish lifestyle funded by his ill-gotten gains.
But when a private detective, hired by his angry victims, came close to
catching him, Keith abandoned Clifton and fled to the farthest point on
the rail: St. Louis.
Deeply divided in its loyalties and with its own history of devious
Confederate operations, the city was a harbor for Sandy Keith. Here, the
infamous boat-burners sneaked around the docks, setting fire to Union
ships and placing infernal machines in their coal bunkers. Some 60 ships,
carrying soldiers and innocent civilians, had met their fates in this
way.
Keith undoubtedly felt he had reached a haven in the Southern Hotel,
a cheerful yellow-limestone building between Fourth and Fifth streets.
Theodore Dreiser, who later worked as a news reporter in St. Louis,
wrote that the Southern was �my favorite cure for all despondent gray
days�where all was warm, brisk, colorful, gay, in fact. Here was no
least sign of poverty or want, but only of comfort or luxury.�
But the tentacles of the angry Confederate network were longer than
Keith had ever imagined, and he was tracked down again by the detective.
Keith offered the man a $5,000 bribe, packed his valise with his
remaining cash and ran again, this time 30 miles east to Highland, Ill.,
a small enclave of Swiss immigrants out on the Looking Glass Prairie.
Here he met a small, dark-eyed young woman, Cecelia, the illegitimate
daughter of an imperious St. Louis milliner, Madame Louise Paris. They
married a few months later.
The couple settled down to a happy life, often visiting the bride�s
family in St. Louis. Early one morning in December 1865, sleeping off
the effects of a Christmas party, they were surprised to hear a loud rap
on the door of their room in the Highland boarding house. Keith opened
the door to find a U.S. marshal, a St. Louis detective and one of
Keith�s victims, Luther Rice Smoot, quartermaster of the state of
Virginia. Keith had stolen $25,000 from him. The men muscled Keith to St.
Louis and locked him in the city jail until he could come up with enough
money to satisfy Smoot.
It was a dreadful twist of fate, for if Smoot had never found him,
Keith might have lived a quiet, law-abiding life in Highland. Instead,
fearful that his other victims would hunt him down, he embarked on a
voyage that would lead to murder and mayhem unprecedented in the annals
of crime.
On a wintry day in January 1866, a panicky Keith and his wife made
for New York and boarded a transatlantic ship bound for Germany. For
several years, they lived the high life in Dresden with their stolen
wealth, hobnobbing with expatriate American socialites and Saxon
generals.
The couple burned through the money at a fantastic rate, champagne
flowing like water at their lavish parties, until Keith saw bankruptcy
looming. To prevent a humiliating downfall, he came up with a diabolical
plot inspired by his old days as a Confederate agent: He would have
worthless export goods insured for a huge sum and get them placed aboard
a ship carrying one of his expertly designed clockwork bombs. The ship
would disappear at sea, taking all of its passengers and the evidence of
his swindle with it, and he would collect the insurance money.
Crossing the Atlantic with his deadly time bomb, Keith made at least
two failed attempts to get the plan to work. Then he set his sights on
the steamship Mosel, docked in the German port of Bremerhaven.
On December 13, 1875, traveling under the name William King Thomas,
Keith took his bomb, packed in a barrel, to the harbor. Leaving the
barrel with the porters, he boarded the Mosel and took a first-class
cabin. Outside on the busy quay, the porters loaded the barrel into a
horse-drawn wagon and wheeled it toward the ship. They were attaching it
to a winch when it suddenly fell, hit the dock and exploded with
tremendous force.
Witnesses described the scene as a battlefield. The blast caved in
the bow of the Mosel and buckled the deck of the Simson, its tug. Cabins
and decks were covered with sand, broken glass and pieces of wood and
twisted iron. On the cobbled loading area of the dock, the explosion
produced a black, smoking hole 6 feet deep and 7 feet wide. Body parts
lay everywhere, and the air was filled with the moans of the wounded and
the dying. Keith had killed 81 people and seriously injured 50 more.
From his cabin on the Mosel, Keith heard the blast that signaled his
fatal mistake. He rushed onto the deck and looked down at the carnage he
had caused. He took deep swigs from his schnapps flask before descending
to his cabin.
He sat down and wrote a letter in heavy pencil to his wife: �God
bless you and my darling children, you will never see me to speak to
again.� He wrote another to the Mosel�s captain: �What I have seen today
I cannot stand.� Then he picked up his revolver and pumped two bullets
into his head.
But the world was not yet done with Keith. Rescuers discovered him
groaning in his cabin and took him to the baggage hall that had been
turned into a makeshift hospital. He lay among his victims as suspicious
police inspectors interrogated him and finally extracted a brief
confession.
Five days later, Cecelia was led to her husband in his final hours.
Seeing him in such a terrible state, she begged the doctors, �Kill him!
Kill him!� Then she threw herself at her husband�s side and urged him to
repent, but Keith gave her no satisfaction. Instead, his last words were
�I have been a thick head. The fellows in New York are guilty.�
After Keith�s death, a massive investigation ensued on both sides of
the Atlantic to find his real identity and to establish whether he had
had accomplices. The news spread rapidly in the international press,
striking panic in ocean travelers, who wondered whether they might be
the next victims of a new species of criminal.
In St. Louis, reading the sensational reports, the staff at the
Southern Hotel had an eerie feeling that they might have known the
Dynamite Fiend, described as a fat, bearded gentleman with a voracious
appetite who constantly bragged about his blockade-running days. Seeing
an opportunity for fame, one of the clerks sauntered down to the St.
Louis Republican and told reporters of the mysterious stranger who�d
come to town 10 years earlier. The newspaper ran the story, and the
famous detective Allan Pinkerton, who�d been hired by the Bremen police
to investigate, rushed to St. Louis to confirm it.
In Bremen, the coroner requested permission to preserve the Dynamite
Fiend�s head for scientific purposes. The rest of Keith�s body was
tossed into a pauper�s grave, but the head was placed in a jar and kept
in a Bremen museum. Local legend has it that during an Allied bombing of
the city, the jar burst, dumping the head into the general mayhem, where
it was mixed with newly dismembered body parts. Finally it came to rest
with the �unidentified.�

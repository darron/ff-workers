The Cypress Hills Massacre[1][2][3] occurred on June 1, 1873, near Battle Creek in the Cypress Hills region of Canada&#39;s North-West Territories (now in Saskatchewan). It involved a group of American bison hunters, American wolf hunters or &#34;wolfers&#34;, American and Canadian whisky traders, Métis cargo haulers or &#34;freighters&#34;, and a camp of Assiniboine people. Thirteen or more Assiniboine warriors and one wolfer died in the conflict. The Cypress Hills Massacre prompted the Canadian government to accelerate the recruitment and deployment of the newly formed North-West Mounted Police.
Incident[edit]
The incident began in the spring of 1873 when a small party of Canadian Red River Métis and American wolfers, led by Thomas W. Hardwick and John Evans, was returning from their winter hunt. While they were camped on the Teton River, their horses disappeared overnight. Presuming that their horses had been stolen by &#39;Indians&#39;, the men travelled on foot to Fort Benton, Montana Territory, about five miles, and asked for assistance from the local authorities to retrieve them. The authorities at Fort Benton refused to assist them, however,[4] so Hardwick organised his own expedition and set off to retrieve the stolen horses. The party numbered 13 men, comprising both U.S. and Canadian &#39;free traders&#39; and wolf-hunters.[4]
The group quickly travelled from Fort Benton northward across the border in pursuit of the stolen horses. They eventually arrived at Abe Farwell&#39;s small trading post located in the Cypress Hills region. While there, they encountered George Hammond, a friend of both Evans and Hardwick, who had been selling whisky in the area. Hammond subsequently joined Hardwick&#39;s group in the search for the missing horses.[5]
Farwell had assured Evans that Little Soldier, the leader of a small band of Assiniboine camped near the trading post, had no horses with them. After a brief search it was determined by the group that Little Soldier showed no evidence that he had stolen their horses, so Evans, Hammond, and the rest of the wolfers retired for the night to Farwell&#39;s trading post, where they spent the evening and the next morning drinking Farwell&#39;s whisky with a group of recently arrived Métis freighters.
In the morning Hammond complained that one of Little Soldier&#39;s men had stolen his horse for a second time,[6] and started towards Little Soldier&#39;s camp, insisting that the rest of the wolfers join him to retrieve his horse. The wolfers, along with the Métis, followed Hammond to the Assiniboine camp.
Historical accounts differ on what happened during the skirmish, as there were no reliable testimonies. The best information states that:
Abe Farwell testified that he tried to restrain Hammond in an attempt to avoid any violence.[6]
Hammond approached Little Soldier&#39;s tent asking about the missing horse.
Little Soldier replied that his group had not stolen the horse but that it was grazing on a nearby hill.
Both Little Soldier&#39;s and Hammond&#39;s parties were intoxicated, and negotiations between them fell through. Little Soldier offered Hammond two of his horses as hostages until the missing horse could be found, but the situation became increasingly tense as women and children began fleeing from the camp and Little Soldier&#39;s men began stripping off their garments in preparation for battle.[7]: 135
The wolfers regarded these actions as a signal for a fight and lined up along a riverbank fifty yards outside the Assiniboine camp. In a last-ditch effort to avoid violence, Abe Farwell pleaded with the wolfers, asking them not to start shooting. Before he could continue negotiating with Little Soldier and the wolfers, Farwell saw Hammond fire his rifle. The rest of the wolfers, protected by the tall river bank, then fired volleys into the camp. The Assiniboine, using inferior weapons, returned fire, but were unable to sustain an attack due to the wolfers&#39; protected position.[6]
The total number of casualties reported differs widely in various accounts. One of Hardwick&#39;s men—Ed Legrace—was killed, but the number of Assiniboine casualties was higher. In the personal account of Donald Graham, who joined the wolfers at Fort Benton and travelled with them to the Cypress Hills, he states that 13 of Little Soldier&#39;s men were killed in the exchanges of fire. After the battle, the wolfers buried Legrace in a cabin and set the building ablaze. Some sources claim that Legrace&#39;s wooden coffin still remains there to this day.[8]
The site of the massacre was designated a National Historic Site of Canada in 1964.[2] Artifacts from the Cypress Hills Massacre have also been preserved at nearby Fort Walsh National Historic Site, along with reconstructions of Farwell&#39;s and Solomon&#39;s trading posts.
Aftermath[edit]
North-West Mounted Police, 1900
News of the Cypress Hills Massacre did not reach Ottawa until late August 1873, therefore the event itself was not very well known in Canada. The Canadian government soon took steps to have those involved extradited from the United States and tried for murder, causing confrontation between Canada and the United States. The case languished for some time and then was taken up by the newly created North-West Mounted Police (NWMP). The NWMP at this time was still establishing itself; the battle between the Assiniboine warriors and Hardwick&#39;s group of wolf-hunters at Cypress Hills was one of the catalysts for its creation.
Investigation and trials[edit]
In December 1874, Assistant Commissioner James Macleod was given permission by the U.S. government to enter Helena, Montana Territory to start an investigation into the Cypress Hills Massacre. Depending upon the findings of this investigation, the accused could have faced extradition to Canada to face trial under Canadian law. Seven arrests were made, but two men escaped custody before they could be examined. The remaining men were freed because there was not enough clear evidence to prove anything against them, and the American commissioner refused the extradition request as there was far too much conflicting testimony. The Canadian Commissioner (Macleod) was subsequently charged with false arrest, but this charge was soon dropped.
In June, 1876, shortly after they were released from custody in the United States, two traders and a wolfer crossed the border into Canada and were subsequently arrested and put on trial in Winnipeg. The Crown&#39;s case against them failed, however, once again weakened by insufficient or contradictory evidence. The three men were acquitted, and the case was finally dropped in 1882.[9]
W. E. Cullen, the American commissioner, said at the extradition hearing at Helena: although the &#34;preponderance of testimony is to the effect that the Indians commenced the firing... they were doubtlessly provoked to this by the apparently hostile attitudes of the whites... An armed party menacing their camp, no matter for what purpose, was by no means slight provocations.&#34;[9]
Creation of the North-West Mounted Police[edit]
It is very difficult to measure the impact that the Cypress Hills Massacre had on Canada and the United States. The creation of the North-West Mounted Police (NWMP, later the Royal Canadian Mounted Police), was introduced partially as a result of the massacre. Around this time, the Lieutenant Governor of Manitoba, Alexander Morris, was concerned about perceived threats of violence to uniformed Canadians and Americans conducting geological surveys. As a result, Morris had to call off any further surveying until there was a solution. Unable to find a solution, Morris used the reports of the massacre to call on the Prime Minister of Canada, Sir John A. Macdonald, to create a police force. Already planning to establish a police force in the North-West Territories, Macdonald had envisioned a horse mounted brigade based on the idea of the Royal Irish Constabulary. The force would be small; only 300 men could enlist. For an area that covered 480,000 square kilometres,[9] the force would have to be as mobile as possible. Alexander Campbell, the minister of the interior, did not believe sending an armed police force into the North-West Territories was necessary at this time, causing Morris to fear that any delay in training and deployment could be exacerbated further once winter fell. To force their creation, Morris claimed that the Métis and white settlers in the area around Portage la Prairie and Fort Qu&#39;Appelle were experiencing fear and unrest due to the massacre.[9] On September 25, 1873, the government of Canada passed an order-in-council to appoint nine officers of &#34;Mounted Police Force for the North-West Territories&#34;. Recruitment began immediately, and the North-West Mounted Police was created. With the new police force patrolling the area, the border could no longer be so easily crossed.[9]
The creation of the police force also had a political motive. The investigation into the massacre was to ensure that First Nations in the area were able to trust the Canadian government. The investigation would require international cooperation of two federal governments, and the North-West Mounted Police would take measures to make examples out of international criminals. Although ultimately no prosecution took place, the willingness to seek justice for any Canadian contributed to the establishment of peace between the NWMP and First Nations.[9]
As time went on the Cypress Hills Massacre gradually became part of popular knowledge about Canada&#39;s past even though the event was never fully closed and dealt with.
In media[edit]
Sheridan in the 1860s
When the news of the Cypress Hills Massacre broke into eastern Canada, it introduced a wave of anti-Americanism. In the news, the American men were described as &#34;American gangsters&#34; and &#34;American scums&#34;. The idea that only American frontiersmen could commit this outrage had been maintained in many Canadian written accounts of the massacre. Canadians were not only shocked that the Americans committed such a crime in their country, but now the media was putting an increased negative view on Americans. Canadians believed that Americans would continually murder people on Canadian soil.[10] This fear was summed up by General Philip Sheridan&#39;s infamous statement to Tosawi of the Comanche, &#34;The only good Indians I ever saw were dead.&#34;[10]
Although Canadians also took part in the Cypress Hills Massacre, it remains overlooked in western Canadian history. Even though it took place in Canada, the massacre represented a temporary extension of American frontier mentality into the Canadian northwest.
In fiction[edit]
A fictionalized account of the events of the Cypress Hills Massacre is told in the novel The Englishman&#39;s Boy by Canadian author Guy Vanderhaeghe.[11] The story focuses in part on the character of the &#34;Englishman&#39;s boy&#34;, one of the members of the party of wolfers. While little is known of those involved in the actual event, the novel attributes the cause of the massacre to one Tom Hardwick, the &#34;lead&#34; wolfer. The book was made into a miniseries that first appeared on CBC Television in March 2008.
The movie The Canadians was another fictionalized version. The Cypress Hills Massacre is also used as the plot centrepiece for the Terrance Dicks novel Massacre In the Hills which charts the beginning of the NWMP.

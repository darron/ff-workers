The Dynamite Fiend:
The Chilling Story of Alexander Keith Jr.,
Nova Scotia Spy, Con-Artist &amp; International Terrorist
By Ann Larabee
LUCK and good instincts led
American historian Ann Larabee to uncover the story of one of the 19th-century&#39;s
notorious mass murderers.
The feared &#34;dynamite fiend&#34;
just happens to have deep roots in a famous Canadian family. Rooting
deep in the archives of Bremen, Germany, Larabee discovered the
forgotten 130-year-old story of Alexander Keith Jr., a heartless man who
could have inspired Robert Louis Stevenson&#39;s classic horror story Dr.
Jekyll and Mr. Hyde. Most knew him as a jovial, prosperous businessman,
friend of the town&#39;s well-heeled burghers and a good family man who
loved buying presents for his children.
In reality, he was a swindler,
a spy linked to international terrorism in the American Civil War and a
pitiless murderer.
Larabee&#39;s narrative begins as
a simple story of a well-to-do Keith family in 19th-century Halifax. The
owners of Alexander Keith and Son Brewery were leading citizens and
powerful political voices in colonial Halifax.
Unfortunately, a nephew,
Alexander (Sandy) Keith, was the black sheep of the family. Sandy began
passing himself off as Alexander Keith Jr. and associating with
unscrupulous characters, including Confederate blockade runners who did
their business in Halifax&#39;s posh hotels.
Young Keith had one goal -- to
be as rich as Midas. He connived with the desperate Southern patriots
and swindled millions from them.
When they demanded their money
back, he escaped to New York and lost much of his money on the stock
market. He abandoned his pregnant wife when the city became too
dangerous for him but found a new one and more dupes to con in St.
Louis.
When his enemies closed in on
him, Keith fled to Germany and raised a family under the alias of
William Thomas. In the early 1870s, the money began to run out, and
Keith needed a new swindle to maintain his bourgeois faade.
Up to this point, Larabee&#39;s
story is a commonplace sleazy tale. Yes, Keith was a bad man, but now he
steps into the shoes of infamy. He was, argues Larabee, the first mass
murderer to simultaneously exploit the weakness of one technology and
the strength of another to serve his deadly purpose.
In the 1870s, steamship
technology was new and dangerous. A ship&#39;s boilers sometimes blew up
during the ocean crossing and the vessel was never seen again.
Keith surmised he could over-insure
some goods being shipped to the U.S. and collect when the ship sank. All
he had to do was make sure the ship went under.
To do this Keith took
advantage of another new technology: dynamite. Dynamite was invented in
the 1860s, and Keith believed he could construct a time-bomb in a wooden
barrel, place it on a ship and have it explode when the ship was at sea.
In 1875, Keith arranged to
have his barrel-bomb stowed on a ship set to sail from Bremerhaven,
Germany, to New York. He had been only able to insure it for a few
thousand dollars. Sinking this ship wouldn&#39;t make him rich; it was only
a test of his evil plan.
Catastrophe stuck when
dockworkers dropped the barrel and the ensuing massive explosion left,
&#34;135 orphans, 45 widows and 20 hopelessly maimed victims.&#34;
Keith was on the ship and in
apparent despair tried to commit suicide but lingered on for five days.
He refused to give police a reason for his actions and fears of a
terrorist conspiracy gripped Europe.
Psychologists and writers
speculated on the motives and character of the &#34;Dynamite Fiend.&#34; After
his death, Keith&#39;s head was cut off in the hope that phrenologists could
analyse the bumps on his head.
He never expressed any remorse
or made a confession to his wife. His last words to his doctors were
simply, writes Larabee, &#34;I have had ill luck; that&#39;s all.&#34;
The
Keith family of Halifax was able to disassociate itself from William
Thomas/Alexander Keith Jr. It squashed the story, and he was never heard
from again. That is until now. Thanks, Ann Larabee.







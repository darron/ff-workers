1999 School Shooting
W.R. Myers High School shootingLocationTaber, Alberta, CanadaDateApril 28, 1999 11:30‚Äì12:45Attack typeSchool shooting, murderWeaponsSawed off .22-caliber Ruger 10/22 semi-automatic rifleDeaths1Injured1PerpetratorTodd Cameron SmithMotiveBullying
The W. R. Myers High School shooting was a school shooting that occurred on April 28, 1999, at W. R. Myers High School in Taber, Alberta, Canada. The gunman, 14-year-old school dropout Todd Cameron Smith, walked into his school and began firing at  students in a hallway, then went to the central hub of the school in front of the band room, killed one student and wounded another one. It was the first fatal high-school shooting in Canada since the St. Pius X High School shooting, 24 years earlier.
Shooting[edit]
The incident began when Smith entered the school campus armed with a .22-caliber Ruger 10/22 semi-automatic rifle and 375 rounds of ammunition (350 rounds of live ammunition in his coat pocket and 25 rounds loaded into the rifle).[citation needed] As lunch concluded, he fired at three students in a hallway adjacent to the cafeteria. He fatally shot 17-year-old Jason Lang at point-blank range, and then shot at one other student. Gym coach Cheyno Finnie managed to wrestle Smith to the floor. He was arrested without further incident by a Taber constable, who also served as the school&#39;s resource officer.[1] He was charged with one count of first-degree murder, and two counts of attempted murder.[2]
Aftermath[edit]
Legal proceedings[edit]
Smith&#39;s identity and background were originally protected under Canada&#39;s Young Offenders Act at the time of his arrest.[3] He had dropped out of W.R. Myers High School earlier in the school year. According to court documents, he had suffered severe bullying throughout his school years, including having been doused with lighter fluid and threatened to be set alight when he was in the first grade.[4] He was remembered as being intelligent but socially awkward, and had become &#34;reclusive and extremely fearful&#34;[4] by early adolescence.  His mother said he had been showing signs of depression throughout his childhood.[3] Smith&#39;s family stated that he &#34;snapped&#34; after watching coverage of the Columbine High School massacre, which had occurred eight days prior.[4]
Crown prosecutors attempted to have then 15-year-old Smith tried as an adult with the potential for a life sentence with the possibility of parole in five years. The Crown argued that an adult prison would offer greater educational programs than a youth facility could provide.  The court denied the motion and he was tried as a juvenile.[4]
Following his arrest and before the trial, a medical examination discovered Smith had a heart ailment that required open heart surgery. During the surgery, he suffered a stroke and fell into a coma. After awakening from the coma, he had speaking and eating difficulties and suffered from diminished mental capacity. His case was suspended until he recovered, as both the Crown and defense agreed he could not stand trial.[5] Following a &#34;remarkable recovery&#34;, he was declared suitable to stand trial, and was scheduled to appear in court in September 2000.[6] At his trial, Smith pleaded guilty to all three charges, and was sentenced to three years in prison, and was ordered to live seven years on probation upon his release.[4]
Smith&#39;s release, second arrest[edit]
In March 2005, Todd Cameron Smith was released into a halfway house in Toronto, despite the agreement of the judge that the then 20-year-old remained a threat to society.[2] In August of that year, he walked out of the halfway house, leaving behind a note reading, &#34;I can&#39;t be caged any more. If they find me, they&#39;ll have to kill me. I will never be caged again. Bye. Sorry.&#34;[7] His escape prompted Toronto police to obtain a court injunction allowing them to publicize his identity until such time as he was caught.[2]
However, Smith turned himself in to authorities the following day and was arrested without incident. The waiver allowing the publication of his identity in Canada lapsed upon his recapture, though not before his name was published and released by several news outlets across the country. Following his recapture, Canadian media were required to no longer use Smith&#39;s name or photograph, as they had the previous day.

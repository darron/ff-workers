The Concordia University massacre was a school shooting on August 24, 1992 in which Valery I. Fabrikant, an associate professor of mechanical engineering, killed four colleagues and wounded a staff member at Concordia University in Montreal, Quebec, Canada. He was convicted of murder and sentenced to life in prison.
Overview[edit]
Perpetrator[edit]
Fabrikant was a mechanical engineering associate professor who had worked at Concordia University for 13 years. He had begun as a technician, and long demonstrated disruptive behaviour against students, staff members and other academics, described as ranging from &#34;undesirable to intolerable&#34;, with which the university had struggled to deal. One student claimed in a 1982 filed police report that he had raped her and dislocated her shoulder.[1] In addition, he had presented academic challenges in teaching and supervision. As early as 1989, two people reported him saying, &#34;I know how people get what they want, they shoot a lot of people.&#34;[2]
Relations between Fabrikant and the University became increasingly strained in late 1991 and the spring of 1992. Colleagues became alarmed after he turned his aggression against them. After being denied tenure, Fabrikant charged members of the engineering department with questionable financial dealings and improper credit for research work. Because of his unacceptable behaviour in addition to the charges, he faced dismissal on grounds of intimidation and harassment of fellow staff members. He was also under a contempt of court charge for behavior in a lawsuit against colleagues regarding credit on professional articles.
Incident[edit]
On August 24, Fabrikant entered the Engineering department at Concordia with three guns and ammunition in his briefcase. In his shooting rampage on the ninth floor of the Henry F. Hall Building, he killed Electrical and Computer Engineering Departmental Chair Phoivos Ziogas and professors Matthew Douglass, Michael Hogben, and Aaron Jaan Saber (known as Jaan Saber). Fabrikant wounded Elizabeth Horwood, a departmental staff secretary.[3] None of the victims were personally involved in the controversy which Fabrikant had started.
Fabrikant was charged with murder and assault, convicted and sentenced to life in prison. He was denied parole in 2015 and again in 2022.[4]
Events[edit]
Around 2:30¬†p.m. on Monday, August 24, 1992, Fabrikant walked onto the ninth floor of the Henry F. Hall Building. He was carrying a briefcase that contained three handguns (a snub-nosed Smith &amp; Wesson .38-calibre 5-shot revolver, a German-made 6.35mm Meb pistol with a 6-round magazine, and an Argentinian-made 7.65mm Bersa pistol with an 8-round magazine) and a large amount of ammunition.
First he went looking for Dean of Engineering and Computer Science Srikanta Swamy and M.O.M (Sam) Osman (the chair of the mechanical engineering department). Neither were present. Next, he headed to his own office where he was scheduled to meet Michael Hogben, the president of the Concordia University Faculty Association (CUFA). According to police, Hogben attempted to give Fabrikant a letter setting out the conditions under which he would be allowed to visit the CUFA offices. The letter noted Fabrikant&#39;s access would be restricted because his behaviour was causing those who worked there &#34;considerable distress&#34;. Fabrikant took out his .38 calibre pistol and shot Hogben three times. Hogben fell to the floor and died shortly thereafter.
A faculty colleague, Aaron Jaan Saber, called out from his office nearby. Fabrikant crossed the hall and fired two shots into Saber, who died in the hospital the next day. Back in the hall, heading back to Osman&#39;s office again, he fired at Elizabeth Horwood, wounding her in the thigh. Fabrikant worked his way through the ninth floor corridors to the other side of the building and into the office of Phoivos Ziogas (chair of the electrical and computer-engineering department), who was talking with colleague Otto Schwelb. Fabrikant shot Ziogas twice; he died in hospital a month later. He scuffled with Schwelb, who took control of the pistol. Unaware that Fabrikant had two other guns in his briefcase, Schwelb went back to tend the injured Ziogas.

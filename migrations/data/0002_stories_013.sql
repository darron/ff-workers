From Wikipedia, the free encyclopedia
Roger Wallace Warren (December 17, 1943 ‚Äì July 24, 2019) was a Canadian miner who was convicted of nine counts of second-degree murder in connection to the September 18, 1992 Giant Mine bombings near Yellowknife, Northwest Territories, Canada.  Warren was convicted (in 1995) due to his confession to the Royal Canadian Mounted Police.
In 2003, Warren again confessed to the bombing, saying that he acted alone.[1] This second confession followed the decision by the Association in Defence of the Wrongly Convicted, to drop their investigation of the case.[2]
During testimony at a July 2004 lawsuit (filed by the widows of the victims), Warren blamed poor security, his union (now part of the CAW union[3]) and the company that owned the mine, Royal Oak Mines Incorporated, for provoking him.  He claimed that a simple screen and padlock over a broken window would have dissuaded him, and that he was only capable of the bombing because strike-breakers had been &#34;dehumanized&#34; by his union.  He also claimed that &#34;(his) termination resulted in the deaths of nine men.&#34;[4]
He became eligible for parole in 2010, applied for day parole in mid-March 2014, and was granted after a hearing on June 17, 2014. At the hearing, he expressed regret for the murders.[5][6] He was granted full parole in 2017.[7]
Warren died on July 24, 2019 at the age of 75 in Abbotsford, British Columbia.[7]
Warren was portrayed by Frank Moore in the 1996 CBC Television film Giant Mine.

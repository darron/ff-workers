The Sydney River McDonald&#39;s murders occurred on May 7, 1992. As the event‚Äôs name suggests, a shooting spree happened at the McDonald&#39;s restaurant in Sydney River, Nova Scotia, Canada. This quiet community on Cape Breton Island was not used to such gun violence, usually associated with big cities.
The shooting and robbery left three people dead, and one person survived with life-altering injuries that left them permanently disabled. The deceased victims were Neil Burroughs Jr., Jimmy Fagen, and Donna Warren. Arlene MacNeil survived her headwounds. The shooters stole just over $2,000 CAD from the restaurant‚Äôs safe.
That early morning robbery and shooting was committed by three young men, with one of them, Derek Wood, working for that McDonald‚Äôs restaurant. They were arrested about week later and all were convicted by the end of 1993 in separate trials. Today both Freeman Daniel MacNeil and Darren Muise are out on parole while Wood remains incarcerated. It was one of the highest-profile murder cases in Canada at the time.
Background
The Victims
Neil Burroughs Jr., aged 29, was a maintenance worker that served at McDonald‚Äôs restaurants in Industrial Cape Breton. He lived in Glace Bay with his wife and son. He was originally from the Cape Breton town of  Dominion.[1]
James ‚ÄúJimmy‚Äù Fagan, aged 27, was a janitor from  Sydney. He was arriving for his 1:00 a.m. shift when he was shot.[2]
Arleen MacNeil, aged 20, was a worker at the restaurant. She was a business student at the University College of Cape Breton (now known as Cape Breton University). She lived in the  Point Aconi area of Industrial Cape Breton and was not related to the murderer with the same last name.[3] She was the only one of the four people shot that survived this attack.[1]
Donna Warren, aged 22, from  North Sydney. She was the  swing manager that night. She was a law student. She was the only one working that night that knew the combination to the safe where the money was stored.[2]
Robbery and murders
Derek Wood, 18, an employee of the restaurant along with two friends, Freeman Daniel MacNeil, 23, and Darren Muise, 18, broke into the restaurant after closing, planning to rob the establishment. They murdered their victims with a .22 calibre pistol, several knives, and a shovel. One victim survived after being shot in the face, but was left permanently disabled. Forcing an employee to open the restaurant&#39;s safe, they made off with $2,017.[4]
Community response
The murders put Sydney into the spotlight as this became a national news story. The restaurant was in the shopping district of Sydney River on Kings Road. It reopened on May 14, 1992 after RCMP officers and psychologists accompanied staff through a walk-through of the restaurant.[5] The building was demolished in 2000 and moved down the road to a more accessible location. The site of the former McDonald&#39;s is currently an empty lot.[6]
Trial and sentence
MacNeil and Wood were sentenced to life imprisonment with 25 years before parole eligibility. Muise received 20 years before parole eligibility.[4]
Parole dispositions
On March 29, 2011, the National Parole Board announced their decision to grant Muise day parole. A member stated: &#34;Given the significant and real progress you have made over the years, your case management team is of the opinion that the probability that you commit a crime after your release is low.&#34; He received full parole on November 22, 2012.[7]
On December 29, 2022, McNeil was granted day parole. The National Parole Board had rejected an application for parole by Wood earlier that year, claiming he was &#34;too high a risk to reoffend&#34;.[8]

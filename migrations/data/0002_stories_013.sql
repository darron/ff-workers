Matthew Douglass (a professor of civil engineering known to be close to Dean Swamy), tried to reason with Fabrikant when he arrived at the dean&#39;s office. Fabrikant shot him four times, and Douglass died almost instantly.[1]
Fabrikant took a security guard and another professor (George Abdou) hostage, locked himself in an office, and called an emergency operator. He said he had just committed several murders and wanted to talk to a TV reporter. He stayed on the line for an hour. When he briefly put his gun down to adjust the phone, Abdou kicked it away and the security guard overpowered him.[5]
Aftermath[edit]
The university commissioned two Independent Committees of Inquiry to investigate aspects of the August events. In addition, it established task forces to work on internal policy and procedures. In April 1994, the Independent Committee of Inquiry into Academic and Scientific Integrity, headed by H. W. Arthurs, former president of York University, presented their report, entitled Integrity in Scholarship (informally known as the Arthurs Report). It noted issues at Concordia and other production-driven research institutions. It stated: &#34;We have confirmed the validity of a number of Dr. Fabrikant&#39;s more specific allegations&#34; about financial mismanagement and questions about scholarship practices such as article attribution. The report noted that Fabrikant&#39;s allegations were not motivated by concern for the public good, however. Instead, they were the &#34;ultimate revenge&#34; of a desperate man who had already tried blackmail and threats against officials.[6]
An independent review was conducted by John Scott Cowan, former faculty association president and former vice-president (administration) of the University of Ottawa, who was commissioned to study Fabrikant&#39;s employment history and related human-resource issues at Concordia University. He documented years of behavioural problems by Fabrikant. In addition, he identified problems common to university environments, where disruptive behaviour has sometimes been excused under the mantle of academic freedom and because many of the academics involved had little experience as managers. He noted that often academic administrators were not comfortable as managers and needed additional training as reported in May 1994 on Lessons from the Fabrikant File (also known as the Cowan Report). His report included recommendations for how the university could improve its internal communication and management of academics and departments.[2]
In their investigation of academic credentials, a journalist team from the Montreal Gazette discovered that Fabrikant was not a political dissident as he had claimed. Instead, he had emigrated from the USSR after having been fired from a succession of posts for his threatening and disruptive behaviour.
As a result, Concordia University made a number of changes to its ethics rules and administrative procedures to improve operations, the integrity of scholarship, and management of faculty and staff. In addition, it established conflict-resolution workshops and resources. The university fired Fabrikant 17 days after the rampage.[1]
Fabrikant is serving his sentence at Archambault Prison in Sainte-Anne-Des-Plaines, Quebec. He was denied parole in 2015 and again in 2022.[7]
Legal proceedings[edit]
During his trial, Fabrikant came across as petty, vindictive, unrepentant and vain along with being rude and hostile to the judges, whom he called a &#34;low little crook&#34; and even had the same attitude aimed at the psychiatrists who testified on whether he was sane enough for the proceedings to continue.[3]
During the trial Fabrikant acted as his own lawyer (after firing ten lawyers in the process) and called 75 witnesses for his defense, but in the end it took the jury only seven hours to find him guilty of four charges of first degree murder, attempted murder, and two hostage charges.[8] This legal decision has not stopped him from seeking legal challenges to the court&#39;s decision. In 2011 a Superior Court judge dismissed a suit by Fabrikant against his colleagues that had been filed in 1992.[8] He has filed so many lawsuits while behind bars that Canadian courts have declared him a &#34;vexatious litigant&#34; and his computer access while in prison is limited.[1]
Legacy[edit]
Four large granite study tables were placed in the foyer of the Hall Building as a memorial to the slain professors. A nearby wall plaque commemorates the event.
Concordia&#39;s Board of Governors had earlier adopted a policy banning firearms on the university campus. After Fabrikant&#39;s murders, the university joined the Coalition for Gun Control and gathered signatures for a petition calling for tougher national gun laws. In March 1994 Concordia representatives presented members of Parliament with a 200,000-signature petition to ban the private ownership of handguns in Canada.[9]
In addition, the university adopted new rules governing financial accountability and scientific integrity, improvements in process at the time of the August 1992 events. The Internal Audit function was also restructured.[10]
In 1995 the university adopted &#34;The Code of Rights &amp; Responsibilities&#34; and named an Advisor on the Code. It set out standards of conduct for all members of the University. Further work was done on a new code of ethics, resulting in a partial version of &#34;The Code of Ethics: Guidelines for Ethical Actions&#34; being adopted in 1995. The full version was adopted in 1997.[9]
The university created initiatives related to civil behaviour and conflict resolution, including the Peace and Conflict Resolution Series that began in 2003.[9]
Morris Wolfe&#39;s 2002 article on the events won a Canadian Association of Journalists Award for Investigative Journalism.[5]

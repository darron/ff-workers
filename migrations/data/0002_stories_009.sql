The Wells Gray Provincial Park murders was the mass murder of a family of six, committed by David Shearing in August 1982 while the family was camping in the Clearwater Valley near Wells Gray Provincial Park, about 120¬†km (74.6¬†mi) north of Kamloops in British Columbia.[1] While media coined the term &#34;Wells Gray Murders,&#34; because that is where the human remains were found, the actual murders occurred elsewhere, including Fage Creek, 18¬†km (11.2¬†mi) north of Clearwater and 18¬†km (11.2¬†mi) south of the Wells Gray Park entrance on Clearwater Valley Road.[2] The story was featured as Episode 1 of the CBC television series The Detectives in 2018.
Disappearance[edit]
On August 2, 1982, three generations of a family set out on a camping trip. They were Bob and Jackie Johnson, their two daughters (Janet, 13 and Karen, 11), and Jackie&#39;s parents, George and Edith Bentley. The family last contacted relatives on August 6 when Edith called a second daughter, according to retired RCMP Sgt. Mike Eastham whose book about the investigation was published in 1999.[3] On August 16, Bob Johnson failed to return to work at Gorman Brothers Lumber in Westbank which was considered very unusual for the 44-year-old employee. A week later, he was reported missing to the local police.[4]
Search and murder investigation[edit]
The search centered on Wells Gray Park, where the family had planned to meet up. On September 13, a mushroom picker reported finding a burned-out car near Battle Mountain Road that was similar to the one the Johnsons were driving. When the RCMP officers searched the vehicle, they found the burned bodies of the four missing adults who had been shot in the head with a .22 caliber weapon. In the trunk were the remains of the two girls.
During spring 1983, several innovative methods were used to try to solve the baffling murders. In April, the presumed crime was re-enacted on-site for TV cameras and aired across Canada in hopes that some significant clue would be triggered. In May, police drove a replica of the Bentleys&#39; 1981 Ford camper truck to Ontario and Quebec, as over 300 people had claimed they had seen such a vehicle heading east during the fall and winter. The reward for finding the truck was raised to $7,500 and 10,000 posters were sent out to police detachments and post offices across North America.
A year after the families disappeared, the murderer had still not been caught and the summer of 1983 became one of the worst for tourism in nearby Wells Gray Park. During the Canada Day holiday weekend, the weather was splendid and the media advised tourists not to head to British Columbia&#39;s parks because all campgrounds were full. The exception was Wells Gray Park where only 18 of 105 sites were occupied.[2]
Capture[edit]
On October 18, 1983, the missing truck was found by two forestry workers on an abandoned logging road on Trophy Mountain not far from Wells Gray Provincial Park and, like the Johnsons&#39; car, it had been burned. The police were criticized for embarking on a costly wild-goose chase across the country when, for that whole year, the truck was just a few kilometres from the murder site. It now seemed probable that the killer lived in the Clearwater area as few outsiders would be familiar with the maze of logging roads on Trophy Mountain. The truck was lifted out of the forest by a Sikorsky helicopter and flown down the mountain to a flat-bed truck, then taken to the RCMP crime lab in Vancouver. The media were told that the truck yielded no new clues. Police then conducted a second door-to-door questioning in Clearwater. By then, they had amassed a total of 13,000 tips about the case.[2]
On November 19, 1983, local residents were shocked when David William Shearing (b. 1959), 24, was arrested in Dawson Creek, escorted to Kamloops under guard, and charged with the second-degree murders of the six members of the Johnson and Bentley families. The clue that ended the long search for the killer was gleaned during the Clearwater questioning after the truck was found; one person told police that, over a year earlier, Shearing had enquired about how to re-register a Ford pickup and repair a hole in its door. The police had never revealed that there was a bullet hole in the Bentleys&#39; truck.
Sentencing[edit]
On April 16, 1984, David Shearing pleaded guilty to six counts of murder. In his ten-minute summation, Supreme Court Justice Harry McKay described the crime as &#34;a cold-blooded and senseless execution of six defenceless and innocent people...a slaughter that devastated three generations in a single bound. What a tragedy. What a waste, and for what?&#34; He sentenced Shearing to life in prison with no eligibility for parole for 25 years. This was the maximum possible penalty for second-degree murder and the first time in Canadian history that it had been handed out.[2]
Crimes[edit]
It was only after his sentencing that the true nature of Shering&#39;s crimes was revealed. When questioned by Sgt. Eastham after the sentencing, he said that on August 10, he had stalked the victims at their campsite and shot the adults with a .22-caliber Remington Model 572 pump-action rifle.[5] This allowed him to kidnap the girls who he kept alive for a week, raping Janet, and then killing them: on August 16, Shearing shot Karen in the back of the head; the next day on August 17, he killed Janet the same way.[2]
Parole appeals[edit]
In September 2008, David Shearing was up for parole. The National Parole Board ruled that he still had violent sexual fantasies, had not completed sex offender treatment, and was not ready for freedom.[6] His second application, in 2012, was also rejected. Shearing, now known as David Ennis, applied again in 2014, then withdrew the request a month before the hearing. In the meantime, online and paper petitions garnered 15,258 signatures urging the parole board not to release him. Family members of the Johnsons and Bentleys also appealed for relief from the agony of reliving the tragedy and campaigning against Shearing&#39;s release every few years.[2] His 2021 appeal was again rejected.[7]

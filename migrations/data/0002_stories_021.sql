Warrant officer Patrice Vincent, 53, had served in the military for 28 years and was considering retiring to become a cabinet maker.[8] He had previously served as a refrigeration and mechanical technician engineer as well as a military firefighter.[2] Vincent&#39;s funeral was held on November 1, 2014.[8][9]
A second Canadian Forces member was injured in the attack.
Perpetrator[edit]
Martin Couture-Rouleau (died October 20, 2014, age 25) was a francophone Canadian living with his parents in Saint-Jean-sur-Richelieu at the time of the attack. He was separated from his partner and their young child. Couture-Rouleau had converted to Islam in the previous year, and was praying at a mosque &#34;regularly.&#34;[10] He had owned a small power-washing business that failed.[11]
Legal documents show Couture-Rouleau became a Muslim convert in 2013 and started to refer to himself as Ahmad LeConverti (Ahmad the Converted).[2] Facebook activity and a CBC interview show that he had become a supporter of ISIL. He posted images and links that were anti-American foreign policy, and links to anti-Semitic YouTube videos. Global reported Facebook posts in both English and French about Allah and graphic posters with references to Islam&#39;s superiority over Christianity.[12] A Facebook friend who corresponded with Couture-Rouleau said, &#34;So he was really mad that Canada actually supported the American bombing of [ISIS] in Syria and Iraq so I think that was the main motive in killing that Canadian soldier.&#34;[13][14] Another told Radio-Canada that Couture-Rouleau spent hours on the internet and devoured jihadist literature, and that Couture-Rouleau dreamed of dying as a martyr.[13]
According to information Radio-Canada obtained, he had convinced at least four or five people in his circle of friends to convert to Islam, but the friend said that Couture-Rouleau got carried away with an extreme interpretation of the Qur&#39;an.[13] A friend said he was obsessed with practicing his new religion and trying to convert his friends.[12]
The RCMP became aware of Couture-Rouleau after a relative alerted police to the man&#39;s terrorist leanings.[3] He wanted to travel to Iraq to fight with ISIL the summer before he perpetrated the attack in Quebec.[11][15] His Canadian passport was revoked in June 2014 over concerns that he had &#34;become radicalized after converting to Islam.&#34;[2] RCMP Commissioner Bob Paulson confirmed he was one of 90 Canadians under surveillance[13] by the RCMP because they were suspected of wanting to join terrorist organizations.[16]
Responses[edit]
The federal government immediately labelled the act as a terrorist attack. According to Steven Blaney, Canadian Minister of Public Safety, the attack was &#34;clearly linked to terrorist ideology.&#34;[17] The Prime Minister&#39;s office released a statement that said in part &#34;The individual who struck the two CAF members with his car is known to federal authorities, including the Integrated National Security Enforcement Teams (the Integrated National Security Enforcement Teams are an anti-terrorism police unit). Federal authorities have confirmed that there are clear indications that the individual had become radicalized.&#34;[12]
After the 2014 shootings at Parliament Hill, Ottawa, Prime Minister Stephen Harper gave a nationally televised address in which he said &#34;Warrant Officer Patrice Vincent... was killed earlier this week by an ISIL-inspired terrorist&#34; and &#34;this week&#39;s events are a grim reminder that Canada is not immune to the types of terrorist attacks we have seen elsewhere around the world. We are also reminded that attacks on our security personnel and on our institutions of governance are by their very nature, attacks on our country, on our values, on our society, on us Canadians as a free and democratic people who embrace human dignity for all.&#34;[1] Conservative MP Erin O&#39;Toole said on Power Play that he stands behind the terrorist act label, both in the case of Zehaf-Bibeau and Martin Couture-Rouleau. &#34;Warrant Officer Vincent and Nathan Cirillo weren&#39;t attacked for who they were, they were attacked for what their uniform represented, so inherently that&#39;s terrorism.&#34;[18]
Rouleau&#39;s next-door neighbour said, &#34;Mr. Rouleau&#39;s father, Gilles, is a peaceful man who tried to help his troubled son. She said Mr. Rouleau had been &#34;brainwashed&#34; by online radicals. &#34;I don&#39;t think it&#39;s a terrorist act,&#34; she said. &#34;He needed help. We could see that he needed psychiatric help.&#34;[19] &#34;To convert to a new religion and to adopt such violent, radical ideology suggests that he was influenced by others, in person or online,&#34; said Wagdy Loza, head of the Canadian Psychological Association&#39;s extremism and terrorism section.[20]
In an article on The Daily Beast website, Jacob Siegel equated both Couture-Rouleau and Zehaf-Bibeau, &#34;the killers who brought terror to Canada this week&#34;, with the 2014 New York City hatchet attacker, saying &#34;like the killers who brought terror to Canada this week, the New York attacker is being called a &#34;lone wolf&#34;, meaning he acted essentially on his own, [Couture-Rouleau and Zehab-Bibeau] seem custom made for the stray dog profile, and the kind of terrorist the West could be seeing a lot more of in the future&#34;. He cites a paper by Brian Michael Jenkins of the RAND Corporation. Jenkins described them as &#34;stray dogs,&#34; rather than lone wolves, characterizing them as &#34;misfits&#34; who are &#34;who are moved from seething anger to spontaneous deadly action&#34; by exposure to Islamist propaganda.[21]
Media reactions[edit]
Commentators have cited the attacks against Canadian soldiers in Quebec and Ottawa while arguing against Canadian Government changes to anti-terrorism legislation, although the legislation was prepared long before the attacks occurred and is not in response specifically to the two attacks.
James Baxter, editor-in-chief of ipolitics.ca, an Ottawa political zine, said &#34;the government is exploiting two isolated murders to push through sweeping new surveillance powers&#34; and pointed out &#34;the would-be &#39;terrorists&#39; involved were armed only with a car and a vintage hunting rifle&#34;.[22]
Andre Picard, in an opinion piece for The Globe and Mail, acknowledged the Couture-Rouleau and Zehaf-Bibeau were both attracted to radical elements in Islam, and dismissed arguments that mental illness caused their actions.[23]
Vincent and Nathan Cirillo, the victim in the Ottawa shootings, were jointly named the Canadian Newsmakers of the Year by the Canadian Press.[24]

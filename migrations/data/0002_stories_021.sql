On December 29, 2014, 53-year-old Phu Lam[1] went on a killing spree in Edmonton, Alberta, Canada. He shot to death eight people, including two children, most of whom were his relatives. He then committed suicide at a Vietnamese/Chinese restaurant, VN Express, in which he had a professional interest that allowed after hours access; the restaurant was located in Fort Saskatchewan, just Northeast of Edmonton. [2]
Details[edit]
Lam killed seven relatives, including two children under the age of ten, in a house in north Edmonton. Lam then went into another house in the Haddow neighbourhood of Edmonton and killed Cyndi Duong. He then drove to Fort Saskatchewan where he entered a Chinese/Vietnamese restaurant, where he had after hours access in order to complete kitchen repairs, and committed suicide by shooting himself.[3] Police entered the restaurant at 7:34 a.m. on December 30, where they found Lam&#39;s body.[4]
Victims[edit]
The victims were:
Klarvatten house: Phu Lam&#39;s wife, Thuy-tien Truong (known as Tien Truong), 35, and her eight-year-old son, Elvis Lam; her father Van-dang Truong, 55; her mother, Thi-dau Le, 55; her sister, Thanh-ha-thi Truong (known as Ha Truong), 33; her sister&#39;s daughter, Valentina Nguyen, 3; Viet Nguyen, 41, Tien Truong&#39;s male friend.
Haddow house: Cyndi Duong, 36.
Perpetrator[edit]
Phu Lam (aged 53) had a criminal record dating back to 1987, which included drug and violence-related offenses.[4] At the time of the shooting, he was in bankruptcy proceedings and owed a large amount of money on at least a dozen credit cards due to a gambling problem.[5] Lam was arrested twice in Edmonton, for sexual and domestic assault. The 9mm handgun used in the shooting was registered in 1997 but was stolen in 2006 from Surrey, British Columbia.[3] In November 2012, a complaint was filed against Lam after he threatened to kill his ex-wife Thuy Tien Truong and five other relatives.[5]

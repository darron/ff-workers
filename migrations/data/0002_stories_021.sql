Terror car ramming
Saint-Jean-sur-Richelieu ramming attackLocationSaint-Jean-sur-Richelieu, Quebec, CanadaDateOctober 20, 2014 (2014-10-20) 11:30 a.m. (EDT)Attack typeVehicle ramming, Islamic terrorismWeaponscarDeaths2 (including the perpetrator)Injured1PerpetratorMartin Couture-RouleauMotiveJihadism
The 2014 Saint Jean sur Richelieu ramming attack was a terror car ramming that occurred in Quebec on October 20, 2014. Two Canadian Forces members were hit by a lone wolf terrorist, Martin Couture-Rouleau. Warrant officer Patrice Vincent died from injuries, while another soldier was injured, but survived. The RCMP and the Government of Canada has characterized the homicide as a terrorist act by an Islamic State of Iraq and the Levant-inspired terrorist.[1]
Attack and pursuit[edit]
On October 20, 2014, Martin Couture-Rouleau deliberately rammed a car into a pair of Canadian Armed Forces soldiers in a shopping centre parking lot in Saint-Jean-sur-Richelieu, Quebec, Canada, at 11:30 a.m. ET. He had been seen sitting in his car and watching for over two hours before the attack.[2] A police chase ensued. The Toronto Sun reported that the suspect called 911 during the chase to say that he carried out his acts in the name of Allah.[3]
The chase ended when Martin Couture-Rouleau lost control of his car, rolling it into a ditch and onto its roof. A witness said that a spike belt was deployed. According to witnesses, Couture-Rouleau exited the car and charged a female police officer with a knife.[3] Police shot him up to seven times and he was pronounced dead that evening in hospital.[3]  A large knife was seen stuck in the ground according to eyewitnesses.[4]
There were two victims. Warrant officer Patrice Vincent (53) died in hospital the next day while another unnamed soldier sustained non-life-threatening injures.[5] One of the soldiers was in uniform and one was not.[3]
A statement by Prime Minister Stephen Harper indicated that government and police officials linked the attack to terrorism.[6][7]
Victims[edit]

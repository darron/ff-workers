Terrorist attacks in Ottawa, Canada
Shootings at War Memorial and Parliament HillPart of Terrorism in CanadaParliament Hill&#39;s Centre Block, scene of the attack by Michael Zehaf-Bibeau[1]LocationOttawaOntario, CanadaDateOctober 22, 2014 (2014-10-22) 9:53 a.m. – 9:55 a.m. (EDT)Attack typeShootings, homicide, terrorism[2]Weapons.30-30 Winchester Model 94 rifle[3]Deaths2 (including the perpetrator)[1][4]Injured3[5]PerpetratorMichael Zehaf-Bibeau[6]MotiveIslamic extremism
The 2014 shootings at Parliament Hill were a series of shootings that occurred on October 22, 2014, at Parliament Hill in Ottawa. At the National War Memorial, Corporal Nathan Cirillo, a Canadian soldier and reservist on ceremonial sentry duty, was fatally shot by mentally unwell [7] Islamic terrorist Michael Zehaf-Bibeau. Zehaf-Bibeau then entered the nearby Centre Block parliament building, where members of the Parliament of Canada were attending caucuses. After wrestling with a constable at the entrance, Zehaf-Bibeau ran inside and had a shootout with RCMP officers. He was shot 31 times by six officers and died on scene.[8][9][10] Following the shootings, the downtown core of Ottawa was placed on lockdown and majority of schools in Ottawa were on lockdown while police searched for any potential additional threats.[11][2][12]
The attacker, Michael Zehaf-Bibeau, was a 32-year-old Canadian habitual offender and addiction patient from Montreal. He had been observed by acquaintances exhibiting erratic behaviour.  At the time of the shooting, Zehaf-Bibeau planned to leave Canada for the Middle East and was living in a homeless shelter in Ottawa while waiting for the processing of his Canadian passport application. According to RCMP Commissioner Bob Paulson the &#34;passport issue was central to what was driving&#34; Zehaf-Bibeau.[13]
Zehaf-Bibeau made a video prior to the attack in which he expressed his desire to &#34;kill some soldiers&#34; and his motives as being related &#34;to Canada&#39;s foreign policy and in respect of his religious beliefs.&#34;[14] To acquaintances and co-workers, he had previously expressed support for jihadists and others in the Middle East resisting the West&#39;s intervention, but was not known to the police to be a terrorism risk. In his mother&#39;s opinion, the attack was the &#34;last desperate act&#34; of someone with a mental disorder who felt trapped.[15] Canadian Muslim organizations condemned the attack.[16]
Classified by the RCMP as a terrorist act under the Criminal Code,[2][17] it was the most serious security breach at Parliament Hill since the 1966 parliament bombing.[18] It took place two days after a man used his car to run over two soldiers in Saint-Jean-sur-Richelieu, Quebec, killing one. The two incidents, which attracted international attention, raised concerns about the effectiveness of police actions to prevent terrorist attacks, the prevention of radicalization and the security measures in place at federal and provincial legislatures.
The Canadian government had already prepared a bill to expand the courtroom anonymity and surveillance powers of the Canadian Security Intelligence Service (CSIS), Canada&#39;s intelligence agency, which was due to be introduced the day of the shootings, and was postponed by the event. The government introduced new anti-terrorism measures with the Anti-terrorism Act, 2015.[19] Security at Parliament Hill is to be stepped up. On June 3, 2015, it was reported that &#34;RCMP officers have started openly carrying submachine guns on Parliament Hill as part of a visible increase to Parliament Hill security.&#34;[20]
Background[edit]
On October 20, in the 2014 Saint-Jean-sur-Richelieu ramming attack, Martin Couture-Rouleau attacked two Canadian Armed Forces soldiers. Rouleau was a 25-year-old Québécois who became a Muslim convert in 2013[21] and was a supporter of the Islamic State of Iraq and the Levant (ISIL).[22] Rouleau used his car to run over the two soldiers before being fatally shot by police after an ensuing car chase.[23] Warrant Officer Patrice Vincent, age 53, subsequently died from injuries.[4]
The terror threat level in Canada was on October 21 raised to medium in light of the ramming attack and due to &#34;an increase in online &#39;general chatter&#39; from radical groups including Islamic State and al-Qaeda.&#34;[24] In his address to the nation following the shootings on October 22, Prime Minister Stephen Harper referred to the October 20 incident as an ISIL-inspired terrorist attack.[25] Although both the October 20 and 22 attacks led to the death of Canadian soldiers, the Royal Canadian Mounted Police (RCMP) ruled out any direct connection between the attacks of October 20 and 22.[26]
Shootings[edit]
National War Memorial[edit]
Nathan Cirillo was on sentry duty at the National War Memorial, similar to the sentries in this picture, when he was fatally shot.
Shortly before 10:00 a.m. EDT, on October 22, 2014, witnesses saw Zehaf-Bibeau arrive at the National War Memorial carrying a rifle, which was later identified as a .30–30 Winchester Model 94 lever-action hunting rifle.[27] He was dressed in blue jeans and a black jacket, with a keffiyeh scarf over the lower part of his face.[28][29] He approached Corporal Nathan Cirillo of The Argyll and Sutherland Highlanders of Canada (Princess Louise&#39;s), a reserve infantry unit, who was one of three sentries from the Ceremonial Guard posted at the Tomb of the Unknown Soldier. At close range, Zehaf-Bibeau shot Cirillo twice in the back,[30] fatally wounding him. Cirillo, like all others who have stood sentry at the memorial, carried an unloaded firearm,[31] and had been provided by the CAF with no particular training for such a situation. It was only minutes before the end of the sentries&#39; shift, which ended at 10:00 a.m.[32] Cpl. Kyle Button and Cpl. Branden Stevenson, two other soldiers on sentry duty, attempted to stop Zehaf-Bibeau, but they were shot at by him and forced to flee to the other side of the Memorial.[32]
Zehaf-Bibeau then pulled down his scarf, held his rifle one-handedly over his head and yelled, &#34;For Iraq.&#34;[33] Cpl. Anthony Wiseman, who was nearby, then attempted to stop Zehaf-Bibeau, but Zehaf-Bibeau attempted to run him over.[32] The three soldiers then attended to Cirillo.[34] Along with bystanders, they applied first aid to Cirillo, attempting to resuscitate him, before Paramedic Chief Anthony DiMonte was first on scene, assumed patient care until additional paramedics arrived to provide medical care before Cirillo was taken to hospital.[32]
Meanwhile, Zehaf-Bibeau returned to his vehicle, a small grey Toyota car, which was parked on the south side of Wellington Street behind the memorial, and drove west along Wellington a short distance to Parliament Hill, where he abandoned his vehicle. Past scattering bystanders, he ran through a gate in the fence surrounding the Parliament Hill precinct and carjacked a parliamentary vehicle assigned to ministers of the Crown, which he drove to the Centre Block parliament building.[35] RCMP officers on patrol in the precinct witnessed the carjacking and pursued the vehicle to the base of the Peace Tower.[30]
A photograph of Zehaf-Bibeau was taken by a tourist at the war memorial during the shootings.[28] It shows him holding the rifle and wearing a scarf over part of his face, with part of the War Memorial behind him. Ottawa police seized the camera and then took their own photograph of the image using a cellphone. French-Canadian journalist William Reymond received a copy of the photograph via the Internet, anonymously, apparently a copy of a tweet originating from the Ottawa Police of the cellphone picture. Reymond then took a picture of the image as it was displayed on his computer monitor and posted it to Twitter a few hours later after confirming the image.[36] The image was re-tweeted by an ISIL sympathizer, leading to erroneous media reports that the image originated from an ISIL Twitter account.[28]
Parliament buildings[edit]
The Hall of Honour in the Centre Block, where Michael Zehaf-Bibeau was killed in an exchange of gunfire
Zehaf-Bibeau entered the Centre Block through the main entrance under the Peace Tower, where Samearn Son was one of two constables on duty. Son saw the rifle in Zehaf-Bibeau&#39;s hand, immediately grabbed it, and pulled it towards the floor while yelling, &#34;Gun! Gun! Gun!&#34; In the ensuing struggle, Son was shot in the foot and had to let go of the attacker.[37] While other RCMP officers converged on Zehaf-Bibeau, Son limped out of the building and calmly told a CBC reporter outside, &#34;I will survive.&#34;[38] Just inside the Centre Block, Zehaf-Bibeau exchanged gunfire with security personnel and was wounded.[39]
Zehaf-Bibeau then ran along the Hall of Honour corridor toward the Library of Parliament. Pursued by RCMP officers, he passed, on his left, the door to a committee room in which Stephen Harper and the Conservative Members of Parliament (MPs) were meeting. Directly opposite was the door to another caucus room, where leader of the opposition, Thomas Mulcair, and New Democratic Party MPs were gathered. (The Liberals, as the third party, were holding their caucus meeting in a chamber downstairs.) During the shooting, one bullet penetrated the outer doors to the NDP caucus room.[39] Inside their room, NDP MPs dove for cover. Across the hall, Harper was briefly hidden in a closet while Conservative MPs made a barricade at the doors, grabbing flagpoles to use as rudimentary spears.[40][41][42][43]
Finally, Zehaf-Bibeau reached an alcove by the entrance to the Library of Parliament, and hid out of sight of RCMP officers,[44] who ordered him to drop his gun and surrender.[38] The location of the alcove is near the office of the Sergeant-at-Arms of the House of Commons, Kevin Vickers.[45] Vickers, a distinguished policeman, commander and administrator with the RCMP before he joined the staff of the House of Commons in 2005,[46] obtained a 9mm handgun from a lock-box and entered the hall.
Vickers&#39;s security team, which had been chasing Zehaf-Bibeau, yelled to Vickers that the suspect was hiding in the alcove. Vickers immediately ran behind the other side of a nearby column, then dove past the column and fired upward at Zehaf-Bibeau.[47]
Witnesses and reports show RCMP Constable Curtis Barrett, leading the tactical formation, fired the shot that killed Zehaf-Bibeau after Bibeau fired in his direction.[1][45][48][49][50] CBC television cameras on the scene recorded over 30 shots fired in the final gunfight.[38] Footage showed a group of RCMP officers converging near the alcove; two loud gunshots are heard, then a large number of gunshots in rapid succession, then silence.[38]
Vickers was recorded by CBC video footage walking away from the shooting site, going to the Conservative caucus room.[38] Vickers explained to the Conservative caucus what had happened and reportedly said either &#34;I put him down&#34;[41] or &#34;I have engaged the suspect. He is deceased.&#34;[51] According to Vickers&#39;s niece: &#34;This is the first time in his career that he&#39;s shot anyone.&#34;[52] Following the shooting, Harper&#39;s RCMP security detail arrived and evacuated him, while the Parliament buildings were put into lockdown.[53]
Perpetrator[edit]

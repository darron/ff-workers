Michael Zehaf-Bibeau[56] (October 16, 1982 – October 22, 2014) was identified by officials as the perpetrator of the shootings.[57]
Personal background[edit]
According to court documents, he was born Joseph Paul Michael Bibeau to unmarried parents (the surname being his mother&#39;s last name), but, in 1995, his parents legally changed his name to Joseph Paul Michael Abdallah Bulgasem Zehaf-Bibeau to better reflect his heritage.[56][58][59] Initial reports put out by Reuters and US-based networks[60] in the first few hours after the shooting[61] said the shooter was born &#39;Michael Joseph Hall&#39; and changed his name after converting to Islam, but these proved unreliable and were contradicted the next day by Reuters,[62] and the major Canadian news sources in in-depth reporting.[56][58][63][64]
Zehaf-Bibeau grew up in Eastern Canada, including Ottawa and Montreal.[65] His mother is Susan Bibeau, a Quebecois, from Montreal and deputy chairperson of a division of Canada&#39;s Immigration and Refugee Board.
His father is a businessman, Bulgasem Zehaf,[66][67] a Libyan immigrant to Quebec who opened the Tripoli café in Montreal. Zehaf is reported to have fought in the 2011 Libyan Civil War;[66][65] the Washington Times reported that Zehaf had returned to his hometown of Zawiyah in Libya to join the uprising against the Muammar Gaddafi regime.
The couple split up before Zehaf-Bibeau&#39;s birth, but reconciled a short while after and were married.[56] Zehaf and Bibeau divorced in 1999.[66]
Zehaf-Bibeau continued to live in the Montreal area until 2007, when he spent time in Libya before moving to Western Canada to become a miner and labourer.[65]
Zehaf-Bibeau became a habitual offender with an extensive criminal record for several offences, including larceny, drug possession, and parole violations. He had received several criminal convictions, at least one of which resulted in a custodial sentence of 60 days&#39; incarceration.[67] In November 2001, just after his 19th birthday, he was convicted of possessing a false credit card and impaired driving.[68] In 2004, he pleaded guilty to drug possession for marijuana and PCP. He failed to appear at the trial date in 2006, but appeared three years later to plead guilty to marijuana possession and was given a discharge. In 2011, he was charged with robbery and &#34;uttering threats&#34; in Vancouver, but only convicted of the lesser charge of uttering threats.[69] He claimed that he committed the 2011 robbery so that he would be incarcerated, in order to kick his drug habit. He received a psychiatric evaluation, but was determined to be fit to stand for trial.[66][70]
Conversion to Islam and travels[edit]
Baptized a Roman Catholic,[71] he converted to Islam[66] in 2004[66] and had attended Sunni Muslim[72] mosques in the British Columbia cities of Burnaby and Vancouver.[65] A mosque in Vancouver, British Columbia, expelled him. &#39;His behaviour was not normal,&#39; said David Ali, vice-president of Masjid Al-Salaam mosque in nearby Burnaby. He said: &#39;We try to be open to everyone. But people on drugs don&#39;t behave normally.&#39;[73] Also, an acquaintance recalled that he had &#34;erratic&#34; behaviour, he had said &#34;the devil is after him&#34;, and was requested to stop attending the mosque after upsetting religious elders.[74] The acquaintance believed he was mentally ill.[75]
Zehaf-Bibeau wanted to leave Canada. An associate of Zehaf-Bibeau reported he had discussed wanting to go to Libya to study Arabic and Islam.[65] At their October 23 press conference, the RCMP said his mother told them that he wanted to go to Syria to join the anti-government rebels in the Syrian civil war,[65][76][77][78] but she denied this, stating that she told the RCMP that he was intending to travel to Saudi Arabia. &#34;He thought he would be happier in an Islamic country where they would share his beliefs.&#34;[79][80] In a telephone interview with the press, Zehaf-Bibeau&#39;s mother said she grieves for the victims of the attack, not her son. In an email to the press, she also said &#34;I am mad at my son,&#34; and said &#34;he seemed lost and did not fit in.&#34;[81]
Zehaf-Bibeau arrived in Ottawa on or before October 2, ostensibly to pursue securing a passport. Zehaf-Bibeau had applied for a Canadian passport and a background check was in progress. RCMP Commissioner Bob Paulson said this issue was central in driving the attacks.[13] Zehaf-Bibeau, who may also have had Libyan citizenship,[82] applied for a Libyan passport renewal on October 2 and was refused the same day.[55] Zehaf-Bibeau had received a Libyan passport in 2000 and then travelled to Libya in 2007. That document had expired, and Zehaf-Bibeau told officials that he wanted a new passport so he could visit family and friends in Libya.[55]
Before the shootings[edit]
Zehaf-Bibeau had been staying at the Ottawa Mission, a homeless shelter in Ottawa near Parliament Hill. According to other residents of the mission, Zehaf-Bibeau and two other men had been trying to get a vehicle. Others said of Zehaf-Bibeau and the other two men, &#34;You knew they were up to something shifty, pacing around a lot and everything.&#34;[83] The vehicle used by Zehaf-Bibeau was purchased one day before the shootings.[30] One resident of the mission noted that Zehaf-Bibeau&#39;s mood had changed three days before the attacks, and he attributed it to Zehaf-Bibeau going back on drugs. Zehaf-Bibeau reportedly told the other residents that he was &#34;anti-Canadian&#34; and to pray because &#34;the world is ending&#34;.[84] Another resident reported that Zehaf-Bibeau had shown a lot of interest in the Parliament buildings, including asking how easy was it to get into the Parliament buildings.[85]
A day before the attack, multiple witnesses saw Zehaf-Bibeau engaged in a &#34;heated discussion&#34; with another man while waiting to register his purchase of the vehicle used in the shootings. According to a witness, Zehaf-Bibeau said &#34;If soldiers bombed your family, wouldn&#39;t you want to kill them?&#34;. Staff in the registry office asked him to lower his voice or leave. The purchase was disallowed by the office because of his out-of-province identification. The purchase was never registered and Zehaf-Bibeau pasted a piece of junk mail on the car to mimic a temporary license permit.[86]
How Zehaf-Bibeau obtained his gun has not been determined.[87] At the time of the shootings, Zehaf-Bibeau was legally prohibited from possessing or acquiring firearms.[3] Additionally, his previous criminal charges and convictions, history of drug abuse, and lack of a fixed address all would have prevented him from receiving a Canadian Firearms Licence.[3] A knife that Zehaf-Bibeau had with him on October 22 was determined to have originated from a relative&#39;s home.[87]
After the shootings[edit]
On November 15, it was reported that Zehaf-Bibeau&#39;s body had remained at the Ottawa coroner&#39;s office for examination. According to the procedure to be followed by the coroner, the examination must report the entry and exit wound of each gunshot wound, and a toxicology report. The issue of accepting his body for an eventual funeral was problematic for the Ottawa Muslim Association mosque: &#34;while denouncing what he did as a crime, a terrorist attack against a soldier who was serving the country, we say that if he&#39;s believing in God, if he&#39;s a Muslim, then he should be buried,&#34; said imam Samy Metwally. &#34;We will give him a funeral service.&#34;[88] However, on November 18, the coroner in an interview announced that the body was released &#34;over a week ago&#34;. The coroner did not release the details of the autopsy, when the body was released or whom the body was released to, citing legal restrictions.[89] On February 26, 2015, the Ottawa Citizen reported that Zehaf-Bibeau was buried in Zawiya, Libya, by arrangements made by his family.[90]

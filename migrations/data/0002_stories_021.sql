Corporal Nathan Frank Cirillo[91] (December 23, 1989 – October 22, 2014), a 24-year-old Canadian soldier, was killed.[92] He was a Class-A reservist[93] of The Argyll and Sutherland Highlanders of Canada from Hamilton, Ontario. He was, as a chosen member of the Ceremonial Guard, on sentry duty[94] at the Tomb of the Unknown Soldier at the National War Memorial when he was shot. Although several civilians immediately provided assistance for the wounded reservist,[95] Cirillo died in hospital later that morning.[92][96] Cirillo had a standard issue Colt Canada C7 rifle which, in accordance with standard practice, was unloaded.[97]
Samearn Son, a House of Commons constable, was injured. When Zehaf-Bibeau arrived at the Centre Block, he was confronted by Son. Son, who was unarmed, was shot in the foot while trying to wrestle away Zehaf-Bibeau&#39;s gun. Son&#39;s actions caused a critical delay, allowing other security personnel to mobilize to stop Zehaf-Bideau.[98] Son was treated and released by Ottawa Civic Hospital.[97]
Two other unidentified people who were injured in unspecified locations were treated and released at Ottawa Civic Hospital.[5][99]  Corporal Cirillo&#39;s remains are buried in the very middle of the military section of Woodland Cemetery in Burlington, Ontario.[100]
Aftermath[edit]
Ottawa[edit]
Ottawa&#39;s downtown core was put under lockdown while a search began for a potential second shooter.[101] Buildings under lockdown included the Parliament buildings, the University of Ottawa and the United States embassy.[1][49] Initially, police reported a third shooting had taken place near the Rideau Centre mall at the same time. A few hours later, police corrected this mistaken report, stating that only two shootings took place.[101]   The locations of the two shooting incidents: 1. the war memorial, 2. the Centre Block
At a press conference at 14:00, Ottawa police confirmed Cirillo&#39;s death without using his name, pending notification of his family. The RCMP confirmed Zehaf-Bibeau&#39;s death, without his name or any other details, citing the ongoing investigation. The National Defence headquarters, the parliament buildings and other government facilities remained in lockdown while a search of the area took place.[102] Areas around Parliament Hill including the Rideau Centre and the University of Ottawa remained closed for hours with buildings in lockdown, with police searching buildings room by room for shooters. Police lifted the lockdown on all public schools in and around Ottawa&#39;s downtown core at around 5:45 p.m.  Parliament Hill remained under lockdown until well past 21:00 and, even after MPs and staffers were allowed to leave the Centre Block, streets directly adjacent to Parliament Hill and the National War Memorial remained closed to the public the next day.[103] Parliament Hill reopened to the public on October 24, and Parliament Buildings tours resumed on October 27.[104]
Nationally and internationally[edit]
Canadian Armed Forces members in Ottawa, Quebec and Atlantic Canada were told to stay out of uniform when not on active duty,[105][106][107] while members in other parts of Canada were not asked to avoid wearing uniforms.[108][109] Further, Canadian military bases around the country heightened their security.[107] The incident caused cancellation of a ceremony scheduled for later the same day in Toronto, in which Nobel Peace Prize recipient Malala Yousafzai was to receive her certificate of honorary Canadian citizenship from Stephen Harper.[110]
Although security was heightened at the Legislative Assembly of Ontario in Toronto, all of the party leaders agreed that the day&#39;s session of Question Period should continue.[111] In Edmonton, public tours were cancelled for the day at the Legislative Assembly of Alberta and security increased.[112] The Legislative Assembly of British Columbia was placed under restricted access and most constituency offices on Vancouver Island were closed as a precaution.[113] Nova Scotia&#39;s legislature, sitting that day, restricted access in the public gallery to the press and staff of the government and political parties.[114] The National Assembly of Quebec flew its flag at half mast the evening of October 22 as a tribute to Cirillo.[115]
In the United States, security was increased in Washington, D.C.: at the Canadian embassy, United States federal government offices nearby, and the Tomb of the Unknowns at Arlington National Cemetery.[116]  The Australian Federal Police increased security at Australia&#39;s Parliament House, as well as at the Canadian High Commission in Canberra.[117]  Security at the New Zealand Parliament Buildings was also tightened.[118]
Investigation and government response[edit]
Initially, the Ottawa Police Service (OPS) said they were investigating the shooting at the war memorial, while the RCMP was investigating the shooting at Centre Block.[49] However, on October 23, the OPS announced that the shooting had been declared a matter of national security and, as such, the entire investigation would be turned over to the RCMP. It was also announced that the RCMP had in turn asked the Ontario Provincial Police (OPP) to conduct a standard independent investigation into the response of police and security forces.[119][120]
Recalled were reports issued by the Auditor General of Canada on the security of the House of Commons and the Senate.[121][122] While many operational aspects were praised, it was noted that possible jurisdictional confusion could result between the RCMP, Ottawa Police, the House of Commons Security Services, and the Senate Protective Service and that no agency had jurisdiction over the roofs of the parliament buildings.[123] The day after the shooting, Prime Minister Harper pledged to expedite plans to boost security forces&#39; surveillance, detention, and arrest powers;[124] other members of the government indicated consideration was being made toward ways to magnify anti-terrorism laws.
The RCMP on October 23 released security video showing Zehaf-Bibeau&#39;s movements across the Parliament Hill precinct. They also announced that Zehaf-Bibeau was not one of 93 suspected extremists on a RCMP high-risk traveller list, though email written by Zehaf-Bibeau had been found on a computer belonging to an unnamed individual charged with terrorism offences.[125] At this meeting, RCMP Commissioner Paulson said Zehaf-Bibeau&#39;s mother had told the RCMP that he wanted to go to Syria, but she denied this, stating that, in a recorded interview, she had told the RCMP that he had wanted to go to Saudi Arabia.[79]
It was announced by the RCMP on October 26 that they had &#34;persuasive evidence&#34; showing Zehaf-Bibeau&#39;s attack had been &#34;driven by ideological and political motives.&#34;[87] According to the RCMP, Zehaf-Bibeau had recorded a video of himself prior to the attack in which, Commissioner Paulson alleged that &#34;[Zehaf-Bibeau] was quite deliberate, he was quite lucid and he was quite purposeful in articulating the basis for his actions. They were in respect, broadly, to Canada&#39;s foreign policy and in respect of his religious beliefs.&#34; The RCMP did not release the video, which they said they were studying for &#34;its intelligence and evidence value&#34;, but Paulson said they hoped to release it eventually.[14]
Paulson appeared before a parliamentary committee on October 27 to brief MPs. &#34;While we are facing this threat at home, we must focus our efforts on preventing individuals traveling abroad to commit acts of terrorism. Preventing the individuals from traveling is critical. If these individuals return with training and/or battle experience, they pose an even greater threat to Canada and our allies.&#34;[126]
That day, the government, after delay caused by the shootings, introduced a bill to broaden the powers of the Canadian Security Intelligence Service (CSIS). Among other powers, it would allow the agency to operate internationally and introduce a program to revoke Canadian citizenship of dual nationals who are convicted of terrorism.[126] The Canadian Civil Liberties Association urged the government to not over-react: &#34;before we take steps to expand existing police and intelligence power, we need answers about why the power that we have were not used, or were insufficient, to prevent or avoid recent tragedies.&#34;[127] On October 29, Minister of Justice Peter MacKay said the government would work to prevent the promotion of terrorism online.[128]
The media reported on November 13 that security video taken at the Centre Block parliament building showed that Zehaf-Bibeau had taken a tour of the  building three weeks before the attack. The security video was not released to the public.[129] A large knife was found next to Zehaf-Bibeau&#39;s body after he was killed, and it was reported that the RCMP&#39;s working theory of his actions was that he intended to behead someone.[85]
On November 23, the government announced that they would unify the various security forces of the House of Commons and the Senate under one command.[130] The existence of the two separate forces had been identified as a point of failure in the October 22 incident. Other areas suggested to be addressed was the need for better surveillance equipment including a central surveillance officer, and a lack of networking between the forces, including the RCMP.[131]
On December 1, RCMP Commissioner Paulson said the RCMP would not release the video of Zehaf-Bibeau&#39;s alleged motives. Members of Parliament had called for its release, stating that he should not comment on it without releasing it for public discussion. Paulson said releasing a partial transcript was possible.[132] The same day, CBC News reported that Centre Block security video showed that Zehaf-Bibeau had been shot once in an exchange of gunfire with Commons security before running along the Hall of Honour towards the Library of Parliament.[39] On December 3, the Toronto Star, National Post and Winnipeg Free Press major newspapers called for the videos to be released.[133][134]
On December 17, Prime Minister Harper suggested that the attacker may not have been acting alone, but did not elaborate.[135] In an interview on national television, Harper would not confirm he was hidden in a closet during the shootings, but said his first concern was to extricate himself from the location. &#34;All you hear is a whole lot of shooting coming towards you, and you don&#39;t know if that&#39;s a firefight or whether it&#39;s just a bunch of guys with automatic weapons wiping everybody out in their path.&#34; After the incident, Harper&#39;s first call was to his mother to reassure her.[136]
The Ontario Provincial Police started two further investigations into the incident. After October 22, the OPP started a review of the RCMP actions on Parliament Hill. On October 29, the House of Commons Speaker asked the OPP to review the conduct of House of Commons constables. On November 24, the RCMP asked the OPP to review Zehaf-Bibeau&#39;s actions from the Memorial to Parliament Hill. The RCMP will then act on revising its security after the reviews are complete. Until that time, the RCMP has increased its numbers around Parliament by rotating new officers for two-month terms to Parliament Hill security.[137] The OPP report was released on the internet with redactions.[138]
On February 24, 2015, the Canadian House of Commons public safety committee passed a motion to invite RCMP Commissioner Paulson to &#34;publicly display and discuss the video&#34; that Zehaf-Bibeau recorded at a future meeting of the committee.[139] Commissioner Paulson showed the video (minus 18 seconds which the RCMP said might be pertinent to investigations into accomplices) at the public safety committee meeting of March 6. Zehaf-Bibeau is quoted as saying: &#34;This is in retaliation for Afghanistan and because Harper [Prime Minister Stephen Harper] wants to send his troops to Iraq. Canada&#39;s officially become one of our enemies by fighting and bombing us and creating a lot of terror in our countries and killing us and killing our innocents. So, just aiming to hit some soldiers just to show that you&#39;re not even safe in your own land, and you gotta be careful. We&#39;ll not cease until you guys decide to be a peaceful country and stay to your own and stop going to other countries and stop occupying and killing the righteous of us who are trying to bring back religious law in our countries. Thank you.&#34; The video was recorded in the car, in Ottawa, on the day of the shootings and was found in the car abandoned in front of Parliament. Paulson said he believes that someone else was involved, and if they were found to have aided Zehaf-Bibeau, that they would be charged with terrorism offences. Paulson also said a post-mortem test for drugs and alcohol on Zehaf-Bibeau was negative and that he had a long knife tied to his wrist when he was killed by Parliament Hill security.[140]
Paulson further briefed Parliament that the RCMP has over 130 full-time investigators and staff on the case. Paulson reiterated the RCMP&#39;s position that Zehaf-Bibeau was a terrorist under Section 83.01 of the Criminal Code, and that he would have been charged with terrorism offences had he survived. OPP investigators also possess the security video taken inside the Parliament building when Zehaf-Bibeau was killed. The RCMP did not determine the source of the gun Zehaf-Bibeau used. They found witnesses to him loading it into his car.[141]
On May 29, 2015, the RCMP released the full transcript of Zehaf-Bibeau&#39;s video. The missing 18 seconds included Zehaf-Bibeau asking God to praise his actions and curse those he is targeting.[142]
In June 2015, the CBC reported some information from the OPP report. The CBC reported that Zehaf-Bibeau was hit 31 times by police including a shot to the back of the head by RCMP Constable Curtis Barrett after which the RCMP stopped firing. He was then hand-cuffed although he was already dead. Vickers himself shot 15 times. After entering the Centre Block, there was an exchange of gunfire between Zehaf-Bibeau and House of Commons Security Corporal Malo. Zehaf-Bibeau then ran down the Hall of Honour. House of Commons Security Constable Louis Letourneau shot at Zehaf-Bibeau 15 times, hitting him once, an event recorded on Centre Block security video. Four RCMP officers advanced on Zehaf-Bibeau, who took one more shot, narrowly missing RCMP Constable Barrett, then started a barrage at Zehaf-Bibeau. The OPP report concluded that the officers&#39; actions were all justified.[143] The OPP report also contained the information that it was determined that Zehaf-Bibeau shot Corporal Cirillo three times in the back at the War Memorial, not twice as was previously reported.[143]
In October 2015, CTV News reported that it had obtained an &#34;unredacted&#34; copy of the OPP report. In it, it was reported that four RCMP officers were involved in the shootout: Const. Curtis Barrett, Const. Martin Fraser, Sgt. Rick Rozon and Corp. Dany Daigle. According to CTV News, the constables were told after Kevin Vickers was applauded for his actions that &#34;the Kevin Vickers train has left the station but you guys will get internal recognition.&#34; Recognition was to include a commissioner&#39;s commendation and recommendation for a Governor General&#39;s Award. According to CTV, the officers did not receive commendations and were on stress leave.[144] Barrett would eventually be recognized after his involvement in the shooting was confirmed and previously lost.[145]
The Royal Canadian Mounted Police established &#34;Project Savvy&#34; to pro-actively look for threats to Canadian security in open source media, including social networks.[146]
Reactions[edit]
On October 23, MPs in the House of Commons gave a standing ovation to Vickers for his heroism. As Mulcair of the New Democratic Party put it: &#34;Just as we have all borne witness to these horrific acts, we have watched in awe your acts of courage, now an abiding emblem of Canadian strength, values and valour.&#34;[147] The first order of business was a moment of silence for Cirillo.[148] Prior to the day&#39;s session, Harper laid a wreath at the War Memorial; MPs from all parties attended a moment of silence and sang &#34;O Canada&#34; at the Memorial before walking together to Parliament.[149] The flags at Parliament Hill and other federal government buildings were also flown at half-mast.[149]
A National Hockey League (NHL) game to have taken place on October 22 in Ottawa, between the Ottawa Senators and Toronto Maple Leafs, was postponed and rescheduled for November 9.[150] Over the following days, several NHL teams held moments of silence and &#34;O Canada&#34; was sung prior to games in the United States in which no Canadian team was participating.[151][152]  On October 25, pre-game ceremonies were jointly held in Ottawa, Montreal, and Toronto to honour the deceased and first responders.[153] On the same day of the attack, before a NHL game between the Pittsburgh Penguins and the Philadelphia Flyers, the Canadian National Anthem was sung in honour of the attack.[154] The largest Canadian flag was used for a pre-game ceremony between the Ottawa Redblacks and the Montreal Alouettes of the CFL.[155]
A fund to benefit the families of Cirillo and Vincent was started on October 22 with a goal of CDN$10,000. It surpassed its goal by noon the following day, when representatives of Canadian banks called to arrange a donation of $80,000. By October 29, the fund, known as the Stand On Guard Fund, had raised $550,000.[156] The fund raised $705,000 of a newly-set $750,000 goal by the time the fund-raising deadline was reached.[157]
A mosque in Cold Lake, Alberta, was defaced overnight with graffiti. This was immediately cleaned up in a joint effort with local residents.[158] An Ottawa mosque was the scene of two incidents: A member praised Zehaf-Bibeau as a martyr after prayers, leading to the police stopping him for national security reasons the next day. A vandal then destroyed several windows, causing over $10,000 in damages.[159]
As soon as the war memorial was reopened to the public, a makeshift memorial to Cirillo grew in front of it; residents laid out flowers, cards, and wreaths. On October 24, 2014, Cirillo&#39;s body was transported home to Hamilton in an official motorcade along Ontario Highway 401, the portion of which between Trenton and Toronto is known as the Highway of Heroes. The times of the motorcade were publicized and people stood at overpasses to observe and as it passed. Cirillo&#39;s family released a statement and gave thanks to the Canadian Forces for the support offered and to those who came to the soldier&#39;s aid.[160] Thousands attended visitation at the funeral home where Cirillo lay in repose. He was accorded the honour of a full regimental funeral service and funeral procession on October 28 in Hamilton, attended by family, soldiers, and dignitaries.[161]
On October 27, a concert by the National Arts Centre Orchestra (NAC Orchestra) (from Ottawa) and Royal Philharmonic Orchestra (from London) concert was dedicated to Patrice Vincent and Nathan Cirillo and was attended by Prince Charles. The NAC Orchestra dedicated its entire UK tour of five performances to Vincent and Cirillo.[162]
On November 11, the annual Remembrance Day service at the war memorial was held as planned. It was attended by Anne, Princess Royal, along with the Governor General, the Prime Minister, and other dignitaries. It was attended by a larger than normal crowd of 50,000. This was attributed to &#34;a rising tide of concern for Canada&#39;s Forces&#34; since the attacks.[163] There was an increased police presence at the event because of the two killings[164] and military armored vehicles patrolled the area, while military personnel were deployed on nearby rooftops.[163] Persons have continued to leave &#34;bouquets, poppies, photos, poems, written tributes, stuffed animals, a can or two of beer, even a battered hockey stick&#34; at the war memorial, although the government has removed the items several times.[163]
Statements on the shootings[edit]
Canadian[edit]
Immediate reactions came from MPs John McKay: &#34;There was a pop, pop, pop sound so the guards ushered us to the back of the building&#34; and David McGuinty: describing there is a &#34;palpable sense of fear&#34; in Ottawa and &#34;it&#39;s extremely unusual for such an atmosphere to exist in the city&#34;. Ottawa Mayor Jim Watson said it was a &#34;sad and tragic day for the city and country.&#34;[165] Other Canadian politicians and social media also reacted.[166]
Elizabeth II, the Queen of Canada, issued a statement from her and Prince Philip, Duke of Edinburgh, expressing shock and sadness.[167] Her federal representative, Governor General David Johnston, extended his sympathies to the family of the deceased Canadian soldier and gratitude for the &#34;professionalism and courage of our security personnel and emergency responders.&#34;[168]
In a televised address that evening, Harper called the shootings a &#34;brutal and violent attack&#34;[169] and made specific reference to the ISIL-inspired attack on Canadian soldiers two days earlier.[170] Harper insisted that &#34;Canada will not be intimidated&#34; by acts of violence and remained committed to Canada&#39;s efforts &#34;to work with our allies around the world and fight against the terrorist organizations who brutalize those in other countries with the hope of bringing their savagery to our shores. They will have no safe haven.&#34;[170] Harper classified both of the week&#39;s attacks as terrorism in his address to the House of Commons on October 23, saying &#34;we&#39;re all aware and deeply troubled that both of this week&#39;s terrorist attacks were carried out by Canadian citizens, by young men born and raised in this peaceful country.&#34;[171]
Tom Mulcair, the Leader of the Official Opposition, called the shooting a &#34;cowardly act designed to strike at the heart of our democracy&#34; that was &#34;designed to drive [Canadians] to hate.&#34;[147] Mulcair categorized Zehaf-Bibeau as a criminal, not a terrorist: &#34;I don&#39;t think we have enough evidence to use that word.&#34;[172]
Justin Trudeau, the leader of the Liberal Party of Canada, said &#34;Canadians know acts such as these committed in the name of Islam are an aberration of [the Muslim] faith. Mutual respect and admiration will help to prevent the influence of distorted ideological propaganda posing as religion.&#34;[147] He agreed with the RCMP that this was a terrorist attack.[172]
Green Party leader Elizabeth May said in the House of Commons: &#34;I would put money on these being the acts of isolated, disturbed and deeply troubled men who were drawn to something crazy. I do not believe that it was a vast network, or that the country is more at risk today than it was last week. However, that is my opinion. I can be wrong...I am undoubtedly going to be wrong again, but what I would like to suggest is that we wait for answers from the police before we make assumptions, and that we speak calmly, truthfully and openly to all Canadians.&#34;[124]
Premier of Ontario Kathleen Wynne said, &#34;Our belief is that people who are using violence to undermine democracy want us to be silenced and we refuse to be silenced.&#34;[173][174]
A public statement by the Muslim Association of Canada condemned the violence in Ottawa and Quebec: &#34;MAC would like to offer its condolences to the victims of violence over the past week. We are horrified by these acts of violence, especially in the Parliament of our nation&#39;s capital. We stand with all Canadians in condemning these attacks and seeking justice for those responsible.&#34;[175] The National Council of Canadian Muslims also said it: &#34;stands united with Canadians in categorically condemning today&#39;s shooting attacks.&#34;[176]
Considerable media attention was paid to the ideology and pathology of Zehaf-Bibeau. Along with Martin Couture-Rouleau and Justin Bourque, he has been labelled a &#34;lone-wolf terrorist&#34;.[177] Couture-Rouleau and Zehaf-Bibeau were &#34;both self-radicalized and adrift in their lives and connected in their sympathy to radical Islamic ideology.&#34;[178] Other opinions diverged from simply calling Zehaf-Bibeau a terrorist. Andrea Polko, girlfriend of Nathan Cirillo said in a statement on Facebook that the dysfunctional state of the Canadian mental health care system was to blame for the shooting.[179] Vancouver Sun columnist Ian Mulgrew commented on how the incident, as well as a failed bombing attempt in Victoria by persons motivated similarly to Zehaf-Bibeau, showed &#39;gaping holes&#39; in the social security net. &#34;These incidents are examples not of Muslim extremism but of the lack of community support for the dysfunctional of any faith who, with a lack of proper attention or the wrong catalyst, become dangerous. I know which approach would make me feel safer, what I would call real security measures: a social safety net that caught those in obvious need before they went postal, people like Zehaf-Bibeau.&#34;[180] Further divergence of opinion appeared in the criticism of the government&#39;s labelling the shooting as a terrorist attack. James Baxter, editor-in-chief of ipolitics.ca, an Ottawa political &#39;zine, said &#34;the government is exploiting two isolated murders to push through sweeping new surveillance powers&#34; and pointed out &#34;the would-be &#39;terrorists&#39; involved were armed only with a car and a vintage hunting rifle&#34;.[181] Journalist Linda McQuaig said &#34;lots of hype about Canada and our institutions being under attack — even as it got harder to explain the difference between the &#39;terrorist&#39; murders of two soldiers and the &#39;non-terrorist&#39; murders of three RCMP officers in Moncton. The main difference appeared to be that the shooter in Moncton was not a follower of Islam&#34;.[182] Academic Emer O&#39;Toole said there was a double standard in the response by the Harper government relative to the 2014 Moncton shooting saying, &#34;Harper&#39;s minimal statement after Justin Bourque shot and killed three Royal Canadian Mounted Police officers in Moncton, New Brunswick, in June, made no mention of terror. Yet Bourque&#39;s actions bore the hallmarks of a conservative and conspiratorial anti-state worldview reminiscent of Norwegian killer Anders Breivik. There was no appeal to Canadian solidarity in the face of an attack on &#34;our country, our values, on our society&#34;; there was no affirmation to &#34;remain vigilant against those at home and abroad who would harm us.&#34;[183]
A national poll by the Angus Reid organization released on November 25, 2014 revealed that Canadians in general were also split as well on whether the attack and shooting was an act of terrorism or mental illness. 38% felt that it was the result of mental illness, while 36% felt it was terrorism, with 25% undecided. Asked about what the federal government should do as a result, 54% felt that more initiatives should be done to prevent radicalization, while 34% favoured harsher punishments. On how to deal with homegrown terrorists, 54% felt that indefinite incarceration only motivated persons to commit crimes, while 46% felt it prevented violent crime.[184]
American[edit]
President Barack Obama was briefed about the attacks. The White House released a statement that read: &#34;President Obama condemned these outrageous attacks, and reaffirmed the close friendship and alliance between our people.&#34;[117] In the following week, U.S. Secretary of State John Kerry made a visit to Ottawa, which included laying a wreath at the National War Memorial in honour of the soldier killed.
Journalist Glenn Greenwald said that the Canadian government was exploiting the shootings by wasting &#34;no time in seizing on the incident to promote its fear-mongering agenda over terrorism, which includes pending legislation to vest its intelligence agency, CSIS, with more spying and secrecy powers in the name of fighting ISIS.&#34; He also said that the shootings weren&#39;t an act of terrorism, writing that, &#34;... to the extent the term [terrorism] has any common understanding, it includes the deliberate (or wholly reckless) targeting of civilians with violence for political ends. But in this case in Canada, it wasn&#39;t civilians who were targeted. If one believes the government&#39;s accounts of the incident, the driver waited two hours until he saw a soldier in uniform. In other words, he seems to have deliberately avoided attacking civilians, and targeted a soldier instead – a member of a military that is currently fighting a war.&#34;[185] Greenwald&#39;s opinion was in turn criticized by academic Daniel W. Drezner who said that his argument was both banal and incomplete, and that the reason why there was shock in Canada at the attack was that democracies at least claimed to adhere to jus in bello when they prosecuted violent conflicts, whereas some of their opponents did not.[186]
An article in The Daily Beast suggested that the attacks by Couture-Rouleau and Zehaf-Bibeau were connected with the 2014 New York City hatchet attack. Like them, the New York attacker was labelled as a &#34;lone wolf,&#34; although Siegel writes that those who are moved from anger to spontaneous deadly action most often fit the profiles of borderline psychotics more than hardcore believers.[187] He cites a 2010 study by terrorism analyst Brian Jenkins of the RAND Corporation, which states that it would be more appropriate to describe &#34;lone wolves&#34; as &#34;stray dogs&#34; which, &#34;&#34;while still dangerous, skulk about, sniffing at violence, vocally aggressive but skittish without backup.&#34;[187]
British[edit]
Prime Minister David Cameron said he was &#34;appalled by [the] attack in Ottawa&#34; and offered his full support to his Canadian and Commonwealth counterpart, Stephen Harper, and the Canadian people as they dealt with the incident.[188]
An editorial in The Guardian on October 26 said &#34;it is hard to judge the exact significance of this week&#39;s events&#34; and &#34;[Canada&#39;s] diversity is reflected in an acute sensitivity to the risks of conflating the threat of terror with Islam itself, which represents the country&#39;s second largest faith community.&#34;  The editorial ended with:
And only a fortnight ago, its parliament voted to join the coalition in support of the US bombing of Isis targets: the country&#39;s security agencies were clear that they believed the decision would heighten the risk of a terrorist attack.  Lawmakers were drafting legislation to combat radical violence before this latest attack. Politicians should now pause before succumbing to the temptation to rush through laws that further restrict civil liberties in the name of national security. It would be a betrayal of all that Canada represents if it allowed exceptional, and horrific, events to remould that society.[189]
An editorial in The Economist from November 8, 2014, in discussing the impact on US-Canada border security, said:
The attack in Ottawa last month by a lone gunman, who killed a Canadian soldier and stormed parliament, seems likely to make matters worse. Although there is still uncertainty about the motives of the gunman, Michael Zehaf-Bibeau—possibly a deranged outcast, possibly a religious extremist—the United States is reviewing security along the 5,525-mile (8,890km) line which separates the two countries (including the Alaskan land border).[190]
Supranational[edit]
United Nations: The United Nations issued a statement that Secretary-General Ban Ki-moon was &#34;aware of the situation...[and] he hopes the situation will be brought quickly under control by Canadian law enforcement authorities. His thoughts are with the people and government of Canada at this difficult time.&#34;[188]
NATO: Secretary General Jens Stoltenberg issued a press release expressing his shock and sadness at the shootings and condolences for the family of the victim.[191]
NORAD: Commander General Charles H. Jacoby issued a statement that offered condolences: &#34;My heart goes out to our Canadian comrades on this tragic day following the shootings in Ottawa, as well as the hit-and-run attack in Quebec earlier this week, both of which targeted Canadian Armed Forces personnel. We offer our sincerest condolences to the people of Canada.  We share their grief, but also their determination not to be intimidated by these cowardly acts.&#34;[116]
Other sovereign states[edit]
Australia: Both Tony Abbott, the Prime Minister, and Mark Binskin, the Chief of the Defence Force, issued statements of condolence and solidarity with Canada.[192]
France: President François Hollande, who made a state visit to Canada, said: &#34;I want to express all of France&#39;s solidarity with the families of the victims tragically murdered in those terrorist attacks that caused such grief to your country.&#34;[193]
Georgia: The Ministry of Foreign Affairs released a statement expressing &#34;its deep concern over the attack&#34; and stating &#34;its solidarity with the Canadian Government and the Canadian people.&#34;[194]
India: Prime Minister Narendra Modi condemned the attack, saying, &#34;As a nation that has experienced a horrific terrorist attack on its Parliament, we share the sense of outrage and trauma of the people of Canada over the attack on the highest institution of democracy. Canada is one of India&#39;s strongest partners and we will continue to strengthen our cooperation in combating terrorism and other crimes for a safer future for our people.&#34;[195]
Israel: Prime Minister Benjamin Netanyahu offered condolences and support.[196]
Malaysia: Ronald Kiandee, the Deputy Speaker of the lower house of the Malaysian parliament, condemned the attack, saying such incidents to Canada&#39;s parliament &#34;should never have happened&#34;.[197]
New Zealand: Prime Minister John Key offered condolences and solidarity.[196]
Singapore: A Ministry of Foreign Affairs spokesperson condemned the attack and said: &#34;The attacks underscore the need for countries to remain vigilant in dealing with the threat posed by terrorists.&#34;[198]

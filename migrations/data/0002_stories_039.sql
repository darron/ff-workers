The officer likely “saved many lives, including his own,” according to the Independent Investigations Office of B.C. (IIO) report.The IIO released its report on Thursday, assessing the Langley RCMP officer’s decision to shoot the suspect, whom homicide investigators later identified as 28-year-old Jordan Daniel Goggins.“The conclusion that the IIO reached is that the actions of the officer to fatally shoot the effective person in this matter were justified by the entirety of the circumstances,” said Ron MacDonald, IIO’s chief civilian director.In the early hours of July 25, 2022, Goggin went on a shooting spree that left two people dead and two others injured.									Story continues below advertisement

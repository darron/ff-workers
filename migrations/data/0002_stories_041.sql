Homicide police identify victims in fatal South Surrey shooting
Vancouver Sun
2022-08-06
Cayley Dobie
© Provided by Vancouver Sun
Three people were shot, and one was killed at South Surrey Athletic Park, 14600 20th Avenue on July 30, 2022.
Police have identified the three victims in last weekend’s fatal shooting at the South Surrey Athletic Park.                                                                           The shooting happened on Saturday, July 30 at about 2:45 p.m. in the 14600-block of 20th Avenue in Surrey.                                                                           The Integrated Homicide Investigations Team is in charge of the case and has identified the three victims as 26-year-old Harbir Khosa, 20-year-old Robeen Soreni and 19-year-old Jordan Krishna.                                                                           Investigators believe the shooting was targeted and connected to the gang war in the Lower Mainland.                                                                           On Friday, police searched a home near the park. One person was taken into custody but no charges have been laid.                                                                           IHIT is seeking witnesses who may have been in the area of the South Surrey Athletic Park parking lot around 2:30 p.m. on July 30. Police say the victims arrived to the park in a black 2010 BMW X5 and that the shooter likely ran away.
© HANDOUT/IHIT
The Integrated Homicide Investigation Team is looking for witnesses who may have seen this black 2010 BMW X5 at South Surrey Athletic Park on Saturday, July 30 at 2:30 p.m. shortly before a fatal shooting. Investigators believe the suspect likely escaped on foot in the opposite direction of the BMW.
“We’ve established a timeline with the black BMW X5 arriving in the park around 2:27 p.m. and the shooting taking place right after,” said IHIT Sgt. David Lee, “the shooter then fled on foot in a western direction out of the park.”                                                                           Anyone with information about the shooting or the suspect is asked to call the IHIT information line at 1-877-551-IHIT (4448) or by email at ihitinfo@rcmp-grc.gc.ca.                                                                          Related         Two killed, one critically injured in shooting at South Surrey Athletic Park                                                                               More news, fewer ads: Our in-depth journalism is possible thanks to the support of our subscribers. For just $3.50 per week, you can get unlimited, ad-lite access to The Vancouver Sun, The Province, National Post and 13 other Canadian news sites. Support us by subscribing today: The Vancouver Sun | The Province.
